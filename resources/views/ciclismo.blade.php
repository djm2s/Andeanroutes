                                            <h3>Itinerario:</h3>
                                            <p>Salimos en nuestro automóvil privado hacia el lago de Huaypo, ubicado aproximadamente a 1 hora de distancia. El paisaje es maravilloso, con colinas ondulantes y una gran cadena montañosa en el fondo, ya que usamos un viejo camino de tierra muy poco utilizado, todo en medio de cultivos andinos, principalmente granos. Haremos una combinación de conducción y ciclismo. Se tarda aproximadamente una hora en pasar por encima de la ciudad de Maras. Desde allí continuaremos hasta las magníficas terrazas agrícolas de Moray, donde diferentes niveles de terrazas están talladas en cuencos profundos / agujeros hundidos que fueron utilizados por los incas para experimentar y adaptar los cultivos andinos a la altitud. Allí disfrutaremos de un almuerzo campestre y luego regresaremos unos kilómetros hacia Maras, donde luego iremos a las salinas de Salinas. Una fuente termal en la parte superior del valle descarga una pequeña corriente de agua fuertemente cargada de sal, que se desvía hacia las salinas y se evapora para producir una sal que se usa para lamer el ganado. Podemos verlos desde el camino y luego descender a ellos. Aquí dejamos las bicicletas y caminamos en medio de los estanques de sal hasta el valle del río Urubamba.</p>
