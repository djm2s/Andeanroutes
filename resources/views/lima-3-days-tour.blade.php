                                            <h5>ITINERARY:</h5>
                                            <p><strong>Day 01: Lima City Tour</strong></p>
                                            <p>Upon your arrival to Lima, your local guide greets you to drive you to hotel. After you have had a chance to check-in to your hotel and freshen up you will meet up with your guide to begin with an overview of the historic parts of Lima, now undergoing restoration which include the PLaza de Armas recently has undergone a renaissance where you will see elegant wooden balconies and colonial buildings you will also see the Cathedral and the Franciscan Monastery and its catacombs. Then visit of modern Lima with the beautiful district of Miraflores. In miraflores the visit of lover´s park with a nice view of the beaches of Lima. <strong>Overnigth in Lima.</strong></p><br>

                                            <p><strong>Day 02: Larco Museum - Enrico Poly</strong></p>
                                            <p>In the morning you will begin the visit with the Larco Herrera Museum founded in 1926, on an old mansion built in 1707. This museum harbors the biggest private collection of pre-columbine art that exists in the world. Exhibition of archeological pieces of ceramic, gold , stone, wood and textile of pre-Hispanic culture, especially the peruvian northen cultures. Outstanding the "Sala de arte Textil", the "gran sala de cultura",. It also exhibits the only collection of world of "Erotic Huacos"... After lunch you proceed to the Enrico Poly Museum, where we view an impressive collection of pre-columbian silver and gold colonial artifacts. <strong>Overnigth in Lima.</strong></p><br>

                                            <p><strong>Day 03: Pachacamac - Amano Museum- Homeward bound.</strong></p>
                                            <p>In the morning we drive to visit Pachacamac, an important Inca religious complex dedicated to worship of the sun god. Then on the way back to Lima we visit the incomparable collection of ancient, central - coast textiles at the by appointment only Museum Amano. According your departure flight back to home transfer to the airport, Is highly recommended to be at the airport 03 hours before of the fly.</p>
