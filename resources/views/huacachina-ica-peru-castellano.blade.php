                                            <h5>ITINERARIO:</h5>
                                            <p><strong>Día 01: Ica y traslado al Valle de los Viñedos.</strong></p>
                                            <p>Dejas Lima y te diriges hacia el sur por la carretera Panamericana conduciendo cómodamente en nuestro autocar privado con aire acondicionado. En el camino visitamos Pachacamac, un importante complejo religioso inca dedicado a la adoración del Dios Sol. Luego continuamos a San José, un pequeño pueblo agrícola donde los cuarteles de los esclavos estaban en tiempos de las haciendas .<br>

                                            <p><strong>Día 02: Exploración de Paracas. </strong> </p>
                                            <p>Esta mañana nuestro guía nos recogerá de nuestro hotel en Paracas y juntos nos trasladarán en vehículo privado unos 20 minutos al aeropuerto de Pisco para nuestro sobrevuelo de las Líneas de Nazca y las cercanas Líneas de Palpa. Después del sobrevuelo, nuestro guía estará esperando para conducir a Ica para nuestro recorrido por la ciudad, incluida una visita especial a la bodega Tacama, abierta desde 1540. Es pionera en el cultivo de uvas para la producción de pisco. Su arquitectura colonial nos llevará a las raíces más profundas del pisco peruano. Tendremos un recorrido por la finca, un campanario, un viñedo y una bodega. También visitaremos una bodega artesanal de pisco llamada "El Catador" en la misma región. Luego visitaremos el pequeño Museo Tello para ver artefactos de la cultura Paracas (BC 1300 - 100 DC) y visitaremos la cercana Necrópolis de Paracas, un cementerio con paredes de adobe donde se encontraron 400 momias y tejidos bien conservados en cámaras con forma de botella. 20 pies debajo de las arenas a la deriva.</p><br>

                                            <p><strong>Día 03: Misterios de Ica y Huacachina</strong> </p>
                                            <p>Hoy podrá observar antiguas vasijas de arcilla conocidas como piscos y la prensa única de roble. En nuestro camino a esta región de viñedos, haremos una incursión en el desierto de Ica para visitar el oasis de Huacachina rodeado de dunas de arena y palmeras. ¡Antes de regresar al hotel, disfrutaremos de un agradable recorrido por las dunas en nuestro vehículo privado 4 * 4! Al mediodía, regreso a Lima.</p>
