                                            <h5>ITINERARY:</h5>
                                            <p><strong>Day 1. Cusco and transfer to the Sacred Valley (Overnight in the Sacred Valley)</strong></p>
                                            <p>An Andean Exclusive representative will pick you up at the airport holding a signboard with your name. Your first stop will be at Awanacancha where you will have the oportunity to see llamas, alpacas, vicunas, and guanacos. Descend towards the Sacred Valley to lower elevations. In the shadows of the mountains east of the Sacred Valley lies the traditional Andean town of Pisac, known for its small, polished stones that fit perfectly together. Visit the traditional market, a thriving hub of commerce that still operates as it has since Incan times, with stalls filling the plaza and locals descending from the highlands to exchange their produce for necessities as well as luxuries in an exchange called trueque. Tour the Pisac ruins, remnants of an important Incan city. In the main ruins, you will find temples of the sun, moon, rain, rainbow and stars, as well as a few Inca crosses rising from the ground. Exploring these ruins means walking a path, which snakes its way along the hillsides. You will hear the history and significance of the site as you enjoy the breathtaking view of countless agricultural terraces leading down to the Urubamba River and the Sacred Valley of the Incas. Your guide will bring you to a local restaurant for lunch. Afterwards, continue to your hotel for check-in.</strong></p>

                                            <p><strong>Day 2. Maras Moray & Salt Mines (Overnight in the Sacred Valley)</strong></p>
                                            <p>Today you will be escorted by private vehicle to the high altitude village of Chinchero town. Here enjoy a guided visit of the town and ruins followed by a gourmet picnic lunch.With the beautiful Urubamba mountain range as your backdrop, saunter through the potato, bean, corn and quinoa fields for approximately two hours, until you reach the mystical ruins of Moray, an incredible series of circular Inca ruins. Following your guided visit of Maras and Moray, transfer back to your hotel.</p>

                                            <p><strong>Day 3. Machupicchu Full Day (Overnight in the Machupicchu)</strong></p>
                                            <p>Today you will be collected from your hotel and driven by private vehicle approximately 20 minutes to the train station in Ollantaytambo to begin your journey to Machu Picchu. As the name implies, the Vistadome train features spectacular panoramic windows to make the most of the incredible scenic views during the amazing journey. This service has a total of five cars and is PeruRail’s mid-range option, offering comfort and quality as well as snacks and refreshments. The focus of the Vistadome is engaging guests with the breathtaking and ever-changing surroundings while giving them the opportunity to take some spectacular photos of the journey. The ride from Ollantaytambo to Aguas Calientes is approximately 1.5 hours.</p>
                                            <p><strong>Day 4. Machupicchu 2nd Visit & Weaving demonstration (Overnight in Cusco City)</strong></p>
                                            <p>Early this morning after breakfast and check-out, meet your guide in the hotel lobby for your second Machu Picchu visit. Exact time of pick-up to be coordinated with your guide the day before. Embark on a hike to Huaynapicchu, Machupicchu Mountain, the Sungate or the Inca Bridge, the views of the site and surrounding landscape are superb. After your visit, continue with your guide who will lead you to the Aguas Calientes train station. Afterwards, continue to your hotel in Cusco for check-in.</p>
                                            <p><strong>Day 5. Cusco City Full Day (Overnight in Cusco City)</strong></p>
                                            <p> Begin your visit in the Plaza de Armas in Cusco’s city center. Visit the renaissance-style Cathedral as well as Koricancha, or the Inca “Sun Temple.” You’ll also get to walk around the San Pedro market before travelling to the outskirts of the city to view the important Inca ruins of Sacsayhuaman. Following the visits, you will be returned to your Cusco hotel. Your Andean Exclusive representative will meet you at your hotel 2 hours before your flight departure and escort you to the airport.</p>
