                                            <h5>ITINERARY:</h5>
                                            <p><strong>Day 1. Cusco Arrival & Sacred Valley (Overnight in the Sacred Valley)</strong></p>
                                            <p>An Andean Exclusive representative will pick you up at the airport holding a signboard with your name. Your first stop will be at Awanacancha where you will have the oportunity to see llamas, alpacas, vicunas, and guanacos. Descend towards the Sacred Valley to lower elevations. In the shadows of the mountains east of the Sacred Valley lies the traditional Andean town of Pisac, known for its small, polished stones that fit perfectly together. Visit the traditional market, a thriving hub of commerce that still operates as it has since Incan times, with stalls filling the plaza and locals descending from the highlands to exchange their produce for necessities as well as luxuries in an exchange called trueque. Tour the Pisac ruins, remnants of an important Incan city. In the main ruins, you will find temples of the sun, moon, rain, rainbow and stars, as well as a few Inca crosses rising from the ground. Exploring these ruins means walking a path, which snakes its way along the hillsides. You will hear the history and significance of the site as you enjoy the breathtaking view of countless agricultural terraces leading down to the Urubamba River and the Sacred Valley of the Incas. Your guide will bring you to a local restaurant for lunch. Afterwards, continue to your hotel for check-in.</p>
                                            <p><strong>Day 2. Machupicchu Full Day (Overnight in Cusco City) </strong></p>
                                            <p>Today you will be collected from your hotel and driven by private vehicle approximately 20 minutes to the train station in Ollantaytambo to begin your journey to Machu Picchu. Upon your arrival in the village of Aguas Calientes, you will board a bus for a winding uphill journey to your final destination. There, enjoy a 2-3 hour guided visit of Machu Picchu before spending the remainder of the day exploring the ruins. Afterwards, return by bus to Aguas Caliente to take your train. Upon arrival at the Poroy train station, you will be welcomed by an Andean Exclusive representative and transferred approximately 30min to your hotel in Cusco.</p>
                                            <p><strong>Day 3. Cusco In & Around - Transfer Out </strong></p>
                                            <p> Begin your visit in the Plaza de Armas in Cusco’s city center. Visit the renaissance-style Cathedral as well as Koricancha, or the Inca “Sun Temple.” You’ll also get to walk around the San Pedro market before travelling to the outskirts of the city to view the important Inca ruins of Sacsayhuaman. Following the visits, you will be returned to your Cusco hotel. Your Andean Exclusive representative will meet you at your hotel 2 hours before your flight departure and escort you to the airport.</p>

