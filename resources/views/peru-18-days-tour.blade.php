                    <h5>ITINERARIO:</h5>
                    <p><strong>DAY 1. Welcome to Lima</strong></p>
                    <p>
                        Arrival on the airport of Lima. You will be welcomed and transferred to your hotel. Stay
                        overnight in Lima.
                    </p>

                    <p><strong>DAY 2.  Lima – Arequipa</strong></p>
                    <p>
                        Flight from Lima to Arequipa. Stay overnight in Arequipa. Including: Breakfast.
                    </p>

                    <p><strong>DAY 03: Colca Canyon Trek 3D/2N (1st day)</strong></p>
                    <p>
                        In the early morning, around 04:00hrs in the morning, you will travel by tourist bus to the
                        <strong>‘Cruz del Condor’</strong> to see the majestic condors. After an hour we will continue
                        to Cabanaconde
                        (journey of approx. 5 hrs). Upon arriving we will have lunch at a restaurant. We start the trek
                        down to the Colca canyon (4 hrs. approx). to the village of San Juan de Chuccho (2.200 m.
                        altitude). We will spend the night in San Juan de Chuccho village in typical huts (home stay)
                        with dinner prepared and served by a local family. Including: Breakfast, Lunch, Diner.
                    </p>
                    <img src="img/tours/arequipa-peru.jpg" class="img-fluid" alt="Tour Nazca lines"><br><br>
                    <p><strong>DAY 04: Colca Canyon Trek 3D/2N (1st day)</strong></p>
                    <p>
                        After breakfast, around 08:00hrs we will be setting out a little early to catch the sunrise over
                        <strong>Colca Canyon,</strong> and also to make time for our stop at the Condor cross to watch
                        for condors. There
                        is also a possibility of spotting condors on the trek up to town from the oasis. The oasis of
                        Sangalle is a great place to relax and appreciate nature as well as enjoying the swimming pools.
                        Stay overnight in basic lodge. Including: Breakfast, Lunch, Diner.
                    </p>

                    <p><strong>DAY 05: Colca Canyon Trek 3D/2N (3rd day) – Puno</strong></p>
                    <p>
                        In the early morning at 04:00hrs, we take the transport back to<strong> Cabanacone.</strong> We
                        will pass the
                        Condor’s Cross and continue to Chivay. From Chivay we will take a private van to Puno. Stay
                        overnight in Puno. Including: Breakfast
                    </p>

                    <p><strong>DAY 06: Titicaca Lake (Floating Reed Islands) – Puno</strong></p>
                    <p>
                        After breakfast we spend the morning adjusting to the altitude, at 3.800m above sea level Puno
                        is Peru’s highest major city. In the afternoon we get the opportunity to visit the floating reed
                        islands of Uros and Taquile to get an impression of how the local people have lived for
                        centuries. These are man-made islands floating on the world’s highest navigable lake, Titicaca.
                        The views are fantastic and the visit will be a memorable experience. Stay overnight in Puno.
                        Including: Breakfast, Lunch
                    </p>
                    <img src="img/tours/puno-peru-islands.jpg" class="img-fluid" alt="Tour En Puno"><br><br>
                    <p><strong>DAY 07: Puno – Cusco</strong></p>
                    <p>
                        After breakfast we drive along the Altiplano to Cusco, the ancient capital of the Inca empire.
                        This drive will take us through sleepy Andean villages, past spectacular snow capped mountains,
                        sheer cliffs and rampaging rivers. We will stop occasionally for you to appreciate the changing
                        scenery. We arrive in the famous city of Cusco in the early evening. Stay overnight in Cusco.
                        Including: Breakfast
                    </p>

                    <p><strong>DAY 08: Cusco (Free Day)</strong></p>
                    <p>
                        Today you have opportunity to explore the city on your own. There are quite a few interesting
                        optional tours (optional – not included) around Cusco, e.g. Maras Moray mountain biking, Via
                        Ferrata (climbing in de Sacred Valley, one of the most exciting adventure experiences being
                        offered in the Urubamba Valley), or river rafting in the Urubamba river in de Sacred Valley.
                        Stay overnight in Cusco. Including: Breakfast<br>

                        * Optional Excursions: City Tour Cusco<br>
                        * Optional Excursions: Visit the community in Huandar<br>
                        * Optional Excursions: Maras Moray mountain biking<br>
                        * Optional Excursions: Via Ferrata, climbing in de Sacred Valley<br>
                        * Optional Excursions: River rafting on the Urubamba river in the Sacred Valley
                    </p>
                    
                    <p><strong>DAY 09: Inca trail 4D/3N (1st day)</strong></p>
                    <p>
                        Today we commence the Inca trail to Machu Picchu. This is likely to be the highlight of any trip
                        to Peru. Machu Picchu is world heritage listed and is known as the most important archaeological
                        site in the Americas. The invading Spanish Armies never made it to Machu Picchu. It was not
                        discovered until early in the 20th century by the explorer and adventurer Hiram Bingham.
                        The path involves high altitude trekking and an average fitness level or above is recommended.
                        We use modern equipment and local, specialist guides. All meals are provided on the trail.
                        Porters will carry all that is required on tour, leaving you to carry your own personal
                        belongings. Extra porters can be hired at an additional cost. The trail involves a spectacular
                        ascension from high altitude mountain passes down into cloud forest.

                        We drive to Km82 to commence the Inca Trail at Piskacuchu (2.680m). From this point we start our
                        trek and we will cross the bridge to walk along the left bank of the Urubamba River, which flows
                        along the north-west side of the Sacred Valley. We continue the Inca trail along a flat terrain
                        and then arrive in Miskay (2.800m). From here we climb to the Inca city of Llactapata (2.650m).
                        Along the way we have spectacular views of the Vilcanota on the opposite side of the Urubamba
                        River, with the impressive Veronica peak (5.682m). We walk through the valley and climb
                        gradually (about five hours) till we arrive at our first campsite at Wayllabamba (3,000m).
                        Includes: Box lunch, Dinner.
                    </p>

                    <p><strong>DAY 10: Inca trail 4D/3N (2nd day)</strong></p>
                    <p>
                        Around 06: 00hrs after breakfast, we will leave towards Wayllabamba (3,000m) to begin the most
                        difficult part of the journey. This consists of an abrupt and steep climb, which extends about 9
                        km. During this climb, the landscape changes from sierra to puna (a dry and high area with
                        little vegetation). On the way to the first mountain pass, the Abra Warmihuañusca what means
                        ‘Dead Woman’s Pass’(4.200m), we see llamas and alpacas graze on Ichu, one of the few plants that
                        grow at this altitude. We also cross an area of the so called cloud forest, the habitat of many
                        different species of birds like hummingbirds and sparrows and the Andean bear, also known as the
                        spectacled bear (Tremarctus Ornatus). Immediately after the pass, we descend to the valley
                        Pacaymayo (3.600m), where we will Stay overnight at the campsite. Includes: Breakfast, Lunch,
                        Dinner
                    </p>
                    <img src="img/tours/camino-inca-inca-trail.jpg" class="img-fluid" alt="Tour En Cusco - Machu Picchu"><br><br>
                    <p><strong>DAY 11: Inca trail 4D/3N (3rd day)</strong></p>
                    <p>
                        This day is the longest day, but also the most impressive and the most interesting, given the
                        number of archaeological sites that we visit. From Pacaymayo we climb to the second pass, the
                        Abra de Runkurakay (3.970m). Halfway we visit the archaeological complex with the same name.
                        This site, located at (3,800m), consists of a small oval structure that is assumed to be served
                        as a watchtower. Then we descend towards Yanacocha (Black Lagoon), on the so-called cloud forest
                        to finally arrive at Sayacmarca (3.624m). This is a beautiful complex consisting of a
                        semicircular construction, enclosures at different levels, narrow streets, liturgical fountains
                        and irrigation canals. We continue the trek with an easy climb, and arrive at the third pass,
                        the Abra of Phuyupatamarca (3.700m). We go through an Inca tunnel to reach the mentioned pass
                        and descend to the complex of the same name. This is one of the most complete and best preserved
                        archaeological complexes along the Inca Trail to Machu Picchu, located on the highest point of
                        the mountain. Phuyupatamarca means ‘City of the Clouds’. We walk towards Wiñaywayna (2.700m). At
                        the campsite we find a lodge with a restaurant, bar and bathrooms with hot showers. Wiñaywayna
                        is an impressive complex consists of an agricultural center with numerous terraces, a religious
                        sector and an urban sector. Wiñaywayna is reaching the last official campsite before we reach
                        Machu Picchu. Make sure you are well rested to be awake in time for the beautiful sunrise at
                        Machu Picchu. Includes: Breakfast, Lunch, Dinner.
                    </p>

                    <p><strong>DAY 12:  Inca trail 4D/3N (4th day)</strong></p>
                    <p>
                        In this fourth and last day we get up at 04:00hrs to leave for Intipunku, The Sun Gate (2.700m).
                        This will be climb for an hour, along a trail of flat stones on the edge of cliffs in the
                        highland jungle. From this fantastic place, we can admire the sunrise over the sacred citadel of
                        Machu Picchu (2.400m). From Intipunku we descend to Machu Picchu, about 40 minutes later we’ll
                        arrive at the citadel from the highest point, the “House of the Guardians’. We then descend to
                        the control point where we register ourselves and leave our backpacks. We start right away with
                        a complete tour of the Inca citadel for about two hours. This includes the Main Plaza, the Royal
                        quarters, the round tower, the Temple of the Three Windows, the Sacred Sun Dial (an extremely
                        essential preserved Intihuatana), warehouses, homes and a large central square. All are
                        connected by narrow stairways and paths bordered by terraces on the hillside.
                        After the tour you have the time to relax yourself and hang out around the archaeological site.
                        In the afternoon we take the shuttle bus to Aguas Calientes. From here we take the train to
                        Ollantaytambo and then a bus to the city of Cusco. Stay overnight in Cusco. Includes: Breakfast.
                    </p>
