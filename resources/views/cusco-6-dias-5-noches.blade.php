                                            <h5>ITINERARIO:</h5>
                                            <p><strong>Día 1. Cusco y traslado al Valle Sagrado (Pernocte en el Valle Sagrado)</strong></p>
                                            <p>Un representante exclusivo andino lo recogerá en el aeropuerto con un letrero con su nombre. Su primera parada será en Awanacancha, donde tendrá la oportunidad de ver llamas, alpacas, vicuñas y guanacos. Descenso hacia el Valle Sagrado para bajar las elevaciones. En las sombras de las montañas al este del Valle Sagrado se encuentra la tradicional ciudad andina de Pisac, conocida por sus pequeñas piedras pulidas que encajan perfectamente. Visite el mercado tradicional, un próspero centro de comercio que aún funciona como lo ha hecho desde la época de los incas, con puestos que llenan la plaza y locales que descienden de las tierras altas para intercambiar sus productos por necesidades y lujos en un intercambio llamado trueque. Recorre las ruinas de Pisac, restos de una importante ciudad inca. En las ruinas principales, encontrarás templos del sol, la luna, la lluvia, el arco iris y las estrellas, así como algunas cruces incas que se elevan del suelo. Explorar estas ruinas significa caminar por un sendero que serpentea por las laderas. Escuchará la historia y la importancia del sitio mientras disfruta de la impresionante vista de innumerables terrazas agrícolas que conducen al río Urubamba y al Valle Sagrado de los Incas. Su guía lo llevará a un restaurante local para el almuerzo. Luego, continúe a su hotel para registrarse.</strong></p>

                                            <p><strong>Día 2. Maras Moray & Salt Mines (Noche en el Valle Sagrado) </strong> </p>
                                            <p> Hoy será escoltado en vehículo privado hasta el pueblo de Chinchero, a gran altitud. Aquí disfrute de una visita guiada de la ciudad y las ruinas, seguido de un almuerzo gourmet de picnic. Con la hermosa cordillera de Urubamba como telón de fondo, pasee por los campos de papa, frijol, maíz y quinua durante aproximadamente dos horas, hasta llegar a las ruinas místicas de Moray, una increíble serie de ruinas incas circulares. Después de su visita guiada a Maras y Moray, traslado de regreso a su hotel.</p>

                                            <p> <strong> Día 3. Machupicchu Full Day (Noche en el Machupicchu) </strong> </p>
                                            <p> Hoy será recogido de su hotel y conducido por un vehículo privado aproximadamente 20 minutos a la estación de tren en Ollantaytambo para comenzar su viaje a Machu Picchu. Como su nombre lo indica, el tren Vistadome cuenta con espectaculares ventanas panorámicas para aprovechar al máximo las increíbles vistas panorámicas durante el increíble viaje. Este servicio tiene un total de cinco autos y es la opción de rango medio de PeruRail, que ofrece comodidad y calidad, así como refrigerios y refrigerios. El enfoque de Vistadome es involucrar a los huéspedes con el entorno impresionante y en constante cambio al tiempo que les da la oportunidad de tomar algunas fotos espectaculares del viaje. El viaje de Ollantaytambo a Aguas Calientes es de aproximadamente 1,5 horas. </p>
                                            <p> <strong> Día 4. 2da Visita a Machupicchu y demostración de tejido. (Pernocte en la ciudad de Cusco) </strong> </p>
                                            <p> Temprano esta mañana, después del desayuno y el check-out, reúnase con su guía en el lobby del hotel para su segunda visita a Machu Picchu. Hora exacta de recogida para coordinar con su guía el día anterior. Embárcate en una caminata a Huaynapicchu, la montaña Machupicchu, el Sungate o el Puente Inca, las vistas del sitio y el paisaje circundante son excelentes. Después de su visita, continúe con su guía que lo llevará a la estación de tren de Aguas Calientes. Luego, continúe a su hotel en Cusco para registrarse. </strong> </p>
                                            <p> <strong> Día 5. Ciudad de Cusco Día completo (Noche en la ciudad de Cusco) </strong> </p>
                                            <p> Explore la ciudad que ha ganado fama mundial y la protección del Patrimonio Mundial de la UNESCO por su fascinante arquitectura y vibrante cultura local. Las paredes y los techos españoles se fusionan armoniosamente con los cimientos incas, lo que sugiere un equilibrio pacífico entre dos culturas que en realidad estaban en desacuerdo y que desmentían la historia verdadera y conflictiva del Cusco. Su guía le proporcionará información y anécdotas sobre las plazas, iglesias y puntos de interés alrededor de la Plaza Principal, así como también recomendaciones personalizadas para ayudarlo a aprovechar al máximo su tiempo en Cusco. Visite la Catedral de estilo renacentista, una de las estructuras más imponentes de la ciudad, y admire la exquisita piedra de Koricancha, o el "Templo del Sol" Inca. También podrá caminar por el mercado de San Pedro antes de viajar a las afueras de la ciudad para ver las importantes ruinas incas de Sacsayhuaman. Después de las visitas, lo regresarán a su hotel en Cusco. </p>
                                            <p> <strong> Día 6. Transferencia fuera </strong> </p>
                                            <p> Su representante exclusivo andino lo recibirá en su hotel 2 horas antes de la salida de su vuelo y lo acompañará al aeropuerto. </p>
