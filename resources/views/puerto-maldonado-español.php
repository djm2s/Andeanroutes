                                                <h5>ITINERARIO: </h5>
                                                <p> <strong> Día 01: Vuelo a Chiclayo desde Lima. </strong> </p>
                                                <p> Al llegar a Lima o Cusco. Le daremos la bienvenida en el aeropuerto y lo llevaremos diez minutos a nuestra sede de Puerto Mladonado. Mientras disfruta del primer sabor del bosque en nuestros jardines, le pediremos que empaquete solo el equipo necesario para sus próximos días y deje el resto en nuestro depósito seguro. Esto nos ayuda a mantener la luz de los barcos y la carga. Bordeando Puerto Maldonado, conducimos 20 kilómetros hasta el puerto del río Tambopata, entrando en la comunidad nativa del infierno. El puerto es un negocio comunal. Transfiera el puerto en bote del río Tambopata al refugio Amazonas, el viaje en bote de dos horas y media desde el puerto de Tambopata al refugio Amazonas pasará la comunidad de Inferno y el punto de control de las reservas nacionales de Tambopata y entrará en la zona de amortiguamiento de esta unidad de conservación de 1.3 millones de hectáreas. almuerzo, a su llegada, el gerente del albergue le dará la bienvenida y le informará con importantes consejos de navegación y seguridad. Cena, búsqueda de Caiman, estaremos en la orilla del río por la noche, explorando las costas con faros y luces para atrapar el rojo destellos de reflejo de los ojos de caimán. <strong> Pernocte en el refugio Amazonas. </strong> </p> <br>
											<p><strong>Día 02:. Cocha - Tour Etnobotánico </strong> </p>
											<p> Desayuno, visita al lago Oxbow: Remaremos alrededor del lago en una canoa o en un catamarán, en busca de vida silvestre a orillas del lago como hoatzin, caiman y hoatzin, caimanes y gritones con cuernos, con la esperanza de ver la oferta que se ve con poca frecuencia aquí. También serás recompensado con carteles aéreos de guacamayos. Torre de Canopy: una caminata de treinta minutos desde el refugio Amazonas conduce al andamio de 25 metros. Torre de dosel. A treinta minutos a pie del refugio Amazonas se llega a la torre de andamiaje de 25 metros. Una escalera con pasamanos que atraviesa el centro proporciona un acceso seguro a las plataformas superiores. La torre se ha construido en un terreno elevado, lo que aumenta el horizonte del bosque primario continuo que se extiende hacia afuera. La Reserva Nacional Tambopata. Desde aquí, es probable que se vean bandadas de dosel de especies mixtas, así como tucanes, guacamayos y aves rapaces. Almuerzo. Visita a la granja: cinco minutos hacia abajo desde los refugios se encuentra una granja propiedad y administrada por el carismático Don Manuel de la comunidad vecina de Condenado. Se siembra una variedad de cultivos populares y desconocidos, apenas alrededor de cada planta y árbol tiene propósito. <strong> Pernocte en Refugio Amazonas.</strong></p><br>
											
                                            <p><strong>Día 03: A Puerto Maldonado - A Lima </strong> </p>
                                             <p> Desayuno; Traslado en bote - Refugio Amazonas al puerto del río Tambopata, traslado del puerto del río Tambopata a Pto. Sede de Maldonado, Transfer Pto. Sede de Maldonado al aeropuerto. Vuelve a rastrear nuestro viaje por el río y el camino de regreso a Pto. Maldonado, nuestra oficina y el aeropuerto. Dependiendo de los horarios de las aerolíneas, esto puede requerir salidas anticipadas.</p>
