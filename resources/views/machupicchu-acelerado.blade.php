                                            <h5>ITINERARIO:</h5>
                                            <p><strong>Día 1. Llegada a Cusco y Valle Sagrado (Pernocte en el Valle Sagrado) </strong> </p>
                                            <p> Un representante de "Andean Exclusive Tours" lo recogerá en el aeropuerto mostrando un letrero con su nombre. La primera parada será en Awanacancha, donde tendrá la oportunidad de ver llamas, alpacas, vicuñas y guanacos. Descenso hacia el Valle Sagrado para bajar las elevaciones. En las sombras de las montañas al este del Valle Sagrado se encuentra la tradicional ciudad andina de Pisac, conocida por sus pequeñas piedras pulidas que encajan perfectamente. Visite el mercado tradicional, un próspero centro de comercio que todavía funciona como lo ha hecho desde la época de los incas, con puestos que llenan la plaza y locales que descienden de las tierras altas para intercambiar sus productos por necesidades y lujos en un intercambio llamado trueque. Recorre las ruinas de Pisac, restos de una importante ciudad inca. En las ruinas principales, encontrarás templos del sol, la luna, la lluvia, el arco iris y las estrellas, así como algunas cruces incas que se elevan del suelo. Explorar estas ruinas significa caminar por un sendero que serpentea por las laderas. Escuchará la historia y la importancia del sitio mientras disfruta de la impresionante vista de innumerables terrazas agrícolas que conducen al río Urubamba y al Valle Sagrado de los Incas. Su guía lo llevará a un restaurante local para el almuerzo. Luego, continúe a su hotel para registrarse. </p>
                                            <p><strong> Día 2. Machu Picchu día entero (Pernocte en la ciudad de Cusco) </strong> </p>
                                            <p> Hoy será recogido de su hotel y conducido por un vehículo privado aproximadamente 20 minutos a la estación de tren en Ollantaytambo para comenzar su viaje a Machu Picchu. A su llegada al pueblo de Aguas Calientes, abordará un autobús para realizar un sinuoso viaje cuesta arriba hasta su destino final. Allí, disfrute de una visita guiada de 2-3 horas a Machu Picchu antes de pasar el resto del día explorando las ruinas. Luego, regrese en autobús a Aguas Calientes para tomar su tren. A su llegada a la estación de trenes de Poroy, un representante de "Andean Exclusive Tours" le dará la bienvenida y lo trasladará aproximadamente 30 minutos a su hotel en Cusco. </p>
                                            <p> <strong> Día 3. Cusco In & Around - Traslado de Salida </strong> </p>
                                            <p> Comience este díacon una visita en la Plaza de Armas en el centro de la ciudad de Cusco. Visite la Catedral de estilo renacentista, así como Koricancha, o el "Templo del Sol" Inca. También podrá caminar por el mercado de San Pedro antes de viajar a las afueras de la ciudad para ver las importantes ruinas incas de Sacsayhuaman. Después de las visitas, lo regresarán a su hotel en Cusco. Su representante exclusivo de Andean lo recibirá en su hotel 2 horas antes de la salida de su vuelo y lo acompañará al aeropuerto.</p>

