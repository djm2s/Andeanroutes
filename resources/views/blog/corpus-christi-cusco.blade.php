                                            <p> It is a feast of the Catholic Church designed to celebrate the Eucharist.
                                                The “Corpus Christi Cusqueño” is the most important religious festival in Cusco, although this date always falls on Thursday, the festival begins a day earlier with the traditional “entrances”. </p>
                                            <br>
                                            <h5>Origin of Corpus Christi in Cusco</h5>
                                            <p>
                                                When the Spaniards arrived in Cusco, they were surprised to learn that the sons of the Sun carried the mummies of the Incas in procession; Then, brandishing the Gospels, they decided to end the pagan festival forever. The solution of the missionaries who accompanied the conquerors was very simple: replace the venerated remains with images of the Virgin and the Catholic saints. Thus, Corpus Christi was born in the ancient capital of Tawantinsuyo, a Catholic celebration that was impregnated with Andean nuances.
                                            </p>
                                            <div class="text-center">
                                                <img src="../img/blog/corpus-christy-bandas.jpg" id="img-blog" alt="Plaza de armas cusco">
                                            </div>
                                            <br>
                                            <br>

                                            <h5>Start of the festivity</h5>
                                            <p>
                                                The start of this festival is Wednesday (one day before the procession or central day) with the departure of each saint from his temple of origin. Each one with a procession where the Mayordomos or the ¨Carguyoq¨ are found, in some cases the mayor, the faithful who voluntarily decide to accompany their saint and accompanied by a band of musicians or ¨Q´aperos¨. After arriving at the Plaza Mayor, the statues of the saints are brought to the Cathedral and remain inside until the next day, which is the procession. On Thursday all these people take their saint or virgin from the Cathedral Basilica and the procession begins. The Plaza Mayor fills up with people. The order in which the saints leave is as follows:
                                            <ul>
                                                <li> 1. San Antonio of the parish of San Cristóbal. </li>
                                                <li> 2. San Jerónimo of the parish of the district of San Jerónimo. </li>
                                                <li> 3. San Cristóbal from the parish of San Cristóbal. </li>
                                                <li> 4. San Sebastián of the parish of the district of San Sebastián. </li>
                                                <li> 5. Saint Barbara of the parish of the Poroy district. </li>
                                                <li> 6. Santa Ana of the parish of Santa Ana. </li>
                                                <li> 7. Santiago Apostle of the parish of Santiago. </li>
                                                <li> 8. San Blas of the parish of San Blas. </li>
                                                <li> 9. San Pedro of the parish of San Pedro. </li>
                                                <li> 10. Saint Joseph of the parish of Bethlehem. </li>
                                                <li> 11. Virgin of the Nativity of the Almudena parish. </li>
                                                <li> 12. Virgen de los Remedios from the church of Santa Catalina. </li>
                                                <li> 13. Purified Virgin of the parish of San Pedro. </li>
                                                <li> 14. Virgin of Bethlehem from the parish of Bethlehem. </li>
                                                <li> 15. Virgin of the Immaculate Conception also called "La Linda" from the Cathedral Basilica. </li>
                                            </ul>
                                            During the procession some carguyoqs give gifts to the assistants, objects such as postcards, souvenirs, etc; they are distributed without distinction. The procession ends at approximately 12 noon after all the saints return to the Cathedral where they will remain until next Thursday when the faithful, the carguyoq, the mayor and others return their saints to their place of origin, the temple. from which they were taken.
                                            </p> <br>
                                            <div class="text-center">
                                                <img src="../img/blog/Corpus-Christi-Cusco-ESCLUSIVE.jpg" id="img-blog" alt="Barrio San Blas Cusco">
