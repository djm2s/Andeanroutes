                                            <h5>Chiriuchu! The typical dish of this holiday!</h5>
                                            <p>
                                                The traditional dish of this festival is Chiriuchu, an exquisite cold and spicy dish for all demanding palates. A mixture of 10 ingredients such as: baked guinea pig (it should be borne in mind that the meat of this rodent must be tender, so that when someone consumes it it is soft since the characteristic flavor is preserved), cau cau (egg cup fish), boiled chicken, beef jerky, qochayuyo (seaweed), chorizo, white corn field, cheese, sliced rocoto and the typical torreja. This spongy roast is the combination of some ingredients such as: corn, squash and a little chicken broth during the preparation so that it has a characteristic flavor when served.
                                            </p>

