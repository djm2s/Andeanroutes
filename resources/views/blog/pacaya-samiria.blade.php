                                            <h5>DESCRIPTION:</h5>
                                            <p>The Pacaya Samiria National Reserve (PSNR) is a protected area located in the Loreto Peru, Peruvian Amazon region. With an area of 2.08 million ha (20,800 square kilometers) is the largest National Reserve of Peru. The extension of the reserve represents 1.5% of the Peruvian territory and 6% of the Loreto region also is somewhat larger than the surface of Slovenia and comparable to 50% of the territory of Denmark, Switzerland, the Netherlands and Costa Rica, the which ensures the ecological and genetic representation of the region. It comprises about 208 population centers with approximately 40 thousand people living in the area and 50 thousand in the buffer zone. There are 56 native communities affiliated indigenous organizations.</p>
                                            <br>
                                            <h5>OBJETIVES</h5>
                                            <p> • Promote studies of flora and fauna in the area.</br>
                                                • Improve and extend education over the area.</br>
                                                • Interest the local population about the benefits of conservation and good wildlife management.</br>
                                                • Promote and improve the use of natural resources according to appropriate ecological development.</p><br>
                                            <H5>WEATHER</H5>
                                            <p>Hot and humid, with temperatures often exceed 34 ° C. As throughout the Amazon, has two distinct seasons: the dry season, the dry or summer season (May to October) is known as the Amazonian summer for the formation of river beaches of white sand and the other is the growing or season rainy season (November to April), known as the Amazonian winter.</p><br>
                                            <H5>HYDROGRAPHY</H5>
                                            <P>There are two main rivers in the reserve: The Pacaya River, a tributary of the Ucayali, it flows into the left bank of the channel Puinahua. The Samiria River, a tributary of the Marañon, flows through the right side of the river. There are about 80 lakes. At the confluence of the Maranon and Ucayali rivers, before the community Mariscal Castilla, born nominally Elrio Amazon.</P> <br>
                                            <h5>TOURISM</h5>
                                            <p>Currently it has 6 areas of tourist use. Of these, Nauta Caño and Yanayacu-Pucate Tibilo-Pastococha are the most visited by tourists because of its proximity to the cities of Iquitos.En Use Tourist Areas are organized groups of people, dedicated to sound management of Amazonian palm trees and provision tourist services. Regarding the second, these people have been adequately trained by different governmental and international organizations to put together tour packages within the PSNR and provide good care and attention to domestic and foreign visitors. Also, comfortable shelters with capacity to accommodate a small number of visitors (no more than 8), to reduce the impact were conditioned. It is not possible to enter the PSNR self-employed, it is always necessary to acquire the services of a licensed travel agency, a shelter, a cruise (luxury boats) or contact directly with organized groups within the reserve. The minimum time required to visit this protected area is 3 days and is recommended to take 5 to 7 days. It is also possible to enter the reserve as a volunteer ranger.</p><br>
                                            <h5>VOLUNTEERING</h5>
                                            <p>The Pacaya Samiria National Reserve has a volunteer program run by the head of the protected area (itself dependent on the SERNANP). One of the requirements for foreigners is to have an intermediate management Castilian spoken. To access the program will need to contact the head of the national reserve. The volunteer will live with park rangers and support in carrying out their work training local communities, implementation of programs of natural resource management.</p><br>
