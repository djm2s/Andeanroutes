                                            <h5>La fiesta del Sol</h5>
                                            <p>Inti Raymi es una palabra quechua que significa ‘Fiesta del Sol’. Todos los solsticios de invierno (entre el 20 y el 23 de junio) el inca rendía un ritual en honor al sol, máxima divinidad en el Tahuantinsuyo. Pachacutec comenzó esta tradición religioso-cultural en el siglo XV. Según la cosmovisión andina, en esa época el sol volvía a comenzar su ciclo. <br>
                                                Antiguamente el Inti Raymi duraba unos 15 días, se hacían sacrificios y se presentaban bailes o danzas para adorar al "Dios Sol". El último Inti Raymi que se realizó con la presencia del Emperador Inca fue en el año de 1535; un año antes de ¨La Conquista Española¨ el año de 1536. <br>
                                                Actualmente el Inti Raymi es una representación teatral, miles de cusqueños y personas de todas partes del mundo se dan cita a este acontecimiento que es una de las manifestaciones culturales y tradicionales más importantes en esta parte del mundo. Esta celebración Inca hace que cada uno de nosotros sienta correr por sus venas la sangre Inca y revalore nuestro pasado.</p>
                                            <br>
                                            <h5>¿Cuando se celebra?</h5>
                                            <p>Los 24 de junio de cada año, el Cusco celebra el Inti Raymi. Esta fecha se debe a que coincide con el Día del Campesino. Es la prenda más vistosa que tienen las fiestas jubilares del Cusco para mostrarle al mundo. </p>
                                            <br>
                                            <h5>¿Dónde se celebra?</h5>
                                            <p>La puesta en escena tiene lugar en 3 sitios específicos de Cusco con gran significado histórico:</p>
                                            <br>
                                            <h6>1° Qoricancha</h6>
                                            <p>El Templo del Sol o Qoricancha, desde las primeras horas del día, inicia la Fiesta del Sol. Esta representación se realiza en el denominado jardín de oro. Empieza con la aparición del inca. Los primeros rituales ofrendan hojas de coca, llamas, alpacas y más.</p>
                                            <div class="text-center">
                                                <img src="../img/blog/intiraymi-cusco-festivity.jpg" alt="Ausangate snowy">
                                            </div><br><br>
                                            <h6>2° Plaza de armas</h6>
                                            <p>El centro histórico del Cusco es punto neurálgico de las celebraciones de la Fiesta del Sol. La Plaza de Armas es cordonada. El inca es cargado en andas por ocho sirvientes y realiza la denominada Ceremonia de la Coca. </p>
                                            <div class="text-center">
                                                <img src="../img/blog/intiraymi-24-de-junio-tours-fiesta-del-sol.jpg" alt="Ausangate snowy">
                                            </div><br><br>
                                            <h6>3° Sacsayhuaman</h6>
                                            <p>La explanada Chuquipampa en la fortaleza de Sacsayhuaman es escenario principal de la representación del Inti Raymi. Este lugar gozó de mucha importancia durante el incanato. </p>
                                            <div class="text-center">
                                                <img src="../img/blog/Inti-Raymi-vista-aerea.jpg" alt="Ausangate snowy">
                                            </div><br><br>

                                            <h5>Datos Curiosos</h5>
                                            <h6>1.- Inti Raymi es una de las fiestas más grandes de Perú</h6>
                                            <p>Los números son grandilocuentes. Se estima que la efeméride fue creada en 1430 por el inca Pachacútec. En aquellos tiempos, se prolongaba por dos semanas donde había danzas y ofrendas al dios-sol Inti (de allí el nombre Inti Raymi, que en quechua significa “Fiesta del Sol”). En 2019, cerca de 200 mil personas fueron a Cusco para el festival, que contó con 700 actores en escena.</p>
                                            <h6>2.- La Fiesta del Sol marcaba el inicio de un nuevo año en el calendario inca</h6>
                                            <p>Para los pueblos antiguos, el Inti Raymi representaba un nuevo comienzo al cerrar un ciclo de cosechas y abrir otro. Hoy, el festival es una forma de mantener viva la cultura ancestral y reverenciar la grandeza de las tradiciones cusqueñas.</p>
                                            <div class="text-center">
                                                <img src="../img/blog/INTI-RAYMI-FESTIVITY-exclusive.jpg" alt="Ausangate snowy">
                                            </div><br><br>
                                            <h6>3.- Tres puntos turísticos de Cusco forman parte de la ruta de la celebración</h6>
                                            <p>El espectáculo comienza en el Complejo de Qorikancha, también conocido como Templo del Sol. Este monumento fue erigido en el siglo XV como un templo de adoración al dios Inti y su estructura ya estuvo adornada con placas de oro. De allí, el cortejo colorido pasa por la Plaza de Armas, corazón del centro histórico de Cusco y Patrimonio Cultural de la Humanidad desde 1983. En el lugar también está la Catedral de Cusco y la Iglesia de la Compañía de Jesús. El ápice de la fiesta ocurre en la Explanada de Sacsayhuamán, donde el público ve el espectáculo desde las gradas. En este complejo arqueológico, ubicado a 2 kilómetros de la Plaza de Armas, es posible admirar el magnífico legado arquitectónico dejado por los incas, que construyeron murallas de casi 300 metros valiéndose solo de grandes piedras perfectamente encajadas entre sí. <br>
                                                <strong>Complejo de Qorikancha: calle Santo Domingo, s/n, centro histórico de Cusco Plaza de Armas o Plaza Mayor de Cusco: entre el Portal de Comercio, el Portal Belén y el Portal de Panes Explanada de Sacsayhuamán: cerca de la calle Don Bosco</strong>
                                            </p>
                                            <h6>4.- Inti Raymi hoy es Patrimonio Cultural del país</h6>
                                            <p>La fiesta no siempre tuvo tanto prestigio. En 1572, el virrey Francisco Álvarez de Toledo la caracterizó de pagana y la prohibió. A pesar de este impedimento, el evento siguió realizándose de modo clandestino hasta 1944, cuando su ruta fue reformulada y la fecha entró nuevamente al calendario oficial de la ciudad. La edición de 2019 marca los 75 años de la recreación del festival, que pasó a ser considerado Patrimonio Cultural de la Nación en 2001.</p>
