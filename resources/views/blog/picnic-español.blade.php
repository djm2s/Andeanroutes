                                            <h5>DESCRIPCIÓN:</h5>
                                            <p>Hoy les traigo una publicación que no trata sobre ropa o tendencias de ropa, sino sobre una moda que está entrando con fuerza en Perú. Se trata de picnics al aire libre, una iniciativa que cada día agrega más seguidores, que comparten comidas y conversaciones, y que solo necesitan un espacio para sentarse. Y Perú disfruta especialmente de paisajes exquisitos que combinan su almuerzo con una vista inusual. </p>
