                                            <h5> Inca mythology </h5>
                                            <p> The Incas had a ‘pantheistic’ religion, that is, they believed that the creator God and nature (mountains, valleys, rivers, lightning, thunder, etc.) were actually a single entity or concept. The Incas did not know the writing so that most of the traditions and myths have been transmitted orally from generation to generation. <br>According to the Inca worldview, space or time was divided into three planes:
                                            <ul>
                                                <li> <strong> Hanan Pacha: </strong> This word means ‘the world from above’. It is represented by a condor. It was the heavenly world, reserved for those people who had been righteous during their lives and which was accessed through a bridge made with hair. In this higher world inhabit the most important gods such as Wiracocha, as we will expand later. </li>
                                                <li> <strong> Kay Pacha: </strong> This word means ‘the present world, the here and now’. It is represented by a puma. It is the world where we humans, animals and some Gods like Pachamama, Mama Sara and Pariacaca live. </li>
                                                <li> <strong> Uku Pacha: </strong> This word means ‘the world below, the world of the dead’. It is represented by a snake. In this world, according to Andean mythology, unborn children, the dead and everything that was below the surface of the earth or the sea inhabit. They believed that caves, fountains, and other openings on Earth's surface were actually the portals or connecting lines between the Uku Pacha and the Kay Pacha. The most important God in this world is called ‘Supay’ although the Goddess Mama Cocha and the God Pachacamac also live there. </li>
                                            </ul>
                                            </p>

