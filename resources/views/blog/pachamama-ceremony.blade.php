                                            <h5> A cultural and artistic event </h5>
                                            <p> The first of August is celebrated on Earth Day or in Quechua: "Pachamama Raymi", in the high Andean communities of Ecuador, Peru, Bolivia and Argentina. The Andes region is characterized by its deep cultural and folk legacy from the Inca and pre-Inca cultures that is expressed in its festivals, customs and traditions. <br>
                                                It is a tradition that continues from ancient Inca times. Many people have a close bond with our Mother Earth (Pachamama in Quechua), therefore every August 1 they perform a Pachamama Payment Ceremony that represents the earth in the broadest sense of the word, that is, the entire nature that provides us with the place and resources we need to live. <br>
                                                The Incas had the greatest respect for him, he was one of their deities as was the Inti or Sun God. Before any festivity they performed, they used to make him a previous thanksgiving ceremony, it was the time for the earth to eat, drink and smoke. It was time to give her a lot of what she has given us. </p>
                                            <br>
                                            <div class="text-center">
                                                <img src="../img/blog/pachamama-pago-a-la-tierra.jpg" id="img-blog" alt="Pago a la tierra - Pachamama">
                                            </div>
                                            <br>
                                            <br>

                                            <h5> How is Pachamama day celebrated? </h5>
                                            <p> As part of the Pachamama Day celebrations, the men and women of the high Andean communities gather on August 1 in a ceremony that includes, among other things:
                                            <ul>
                                                <li> A hole or hole dug in the ground, which is where all the offerings and gifts for the Pacha Mama will be placed. This hole has a depth of 50 centimeters and a diameter of approximately 1 meter, depending on the number of gifts and the size of the ceremony. </li>
                                                <li> A wool blanket or ‘Lliclla’ which is the garment with which the gifts will be wrapped, before covering them with earth. </li>
                                                <li> Animal fetuses, generally using lamb, alpaca or llama fetuses. They symbolize fertility. </li>
                                                <li> Beer, food, coca leaf, cigars, confetti, sweets, cookies. </li>
                                            </ul>
                                            During the procession some carguyoqs give gifts to the assistants, objects such as postcards, souvenirs, etc; they are distributed without distinction. The procession ends at approximately 12 noon after all the saints return to the Cathedral where they will remain until next Thursday when the faithful, the carguyoq, the mayor and others return their saints to their place of origin, the temple. from which they were taken. <br>
                                            <strong> The idea is ‘make Pacha Mama eat, drink and smoke’. At the end of the ceremony, wood or firewood is placed in the hole, all the above is wrapped with the lliclla and finally everything is consumed by the fire, to be later covered with earth. Throughout the ceremony people drink beer, consume various typical dishes and celebrate with dance and music. </strong>
                                            </p> <br>
