											<h5>History</h5>
                                            <p> In mid-July the streets of Paucartambo are filled with music, colors, faith and a great religious devotion, the Mamacha del Carmen Festival brings together Peruvian folklore and the largest number of parishioners, between July 15 to 18 of each year Thousands of people congregate with a single feeling to worship the Mamacha del Carmen, celebrating its central day on July 16. There are several versions on the origin of the festival in honor of the Virgen del Carmen, in this blog we tell you some:</p> <br>
											<p>It is said that there were groups of merchants from the highlands, called "Qollas", who exchanged products such as cañiwa, chuño and cheeses with others with sugar, cane liquor, chancaca and others from the coast. Tradition indicates that one day, when the products coming from the highlands were downloaded, one of the Qollas found the head of the image of the Virgen del Carmen among the vessels. This caused surprise and amazement among the settlers. The head of the image was given to a woman who offered lodging to travelers and it was she who ordered the body of the Virgen del Carmen to be made, which remains to this day. Since then the Qollas pay homage and honor to the patron saint. </p> <br>
											<p>Another version refers that in colonial times there were in Paucartambo Spanish estates and colonists who dedicated themselves to the extraction of minerals in the Qosñipata laundries. Noting that diseases such as malaria and others that easily attacked locals were spreading, the landowners saw the need to bring Afro-Peruvian slaves from the coast to carry out such work. These new inhabitants, who were devotees of the Virgen del Carmen, took the image to the haciendas and secretly worshiped it. They say that the patron saint of one of the haciendas discovered them and after becoming a devotee he ordered the construction of a chapel to worship him. This version is supported by the Qhapaq Negro dance, one of the most celebrated on the feast of the Virgin of Paucartambo.
                                                </p>
