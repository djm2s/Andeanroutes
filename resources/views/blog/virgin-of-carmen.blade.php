											<h5>Party program:</h5>
											<h4>July 15</h4>
                                            <p>The party begins with a fireworks show and the sound of bells. Then a troupe enters and the night becomes an accomplice to make a bonfire. Afterwards, a group of villagers stage purgatory through acrobatic jumps, races and different pranks. At 11:00 p.m. the band of musicians performs a serenade to the virgin until the next day.</p> <br>
											<h4>July 16</h4>
                                            <p>It is the central day of this majestic festival. It starts at 5:00 a.m. with an Aurora mass, then at 10:00 a.m. the celebration mass is given where they interpret the songs of the Qollas and the black Qhapac (ancient cultures of Paucartambo). To end this sacred day in the streets you can listen to troupes, dances and songs.</p> <br>
											<h4>July 17</h4>
                                            <p>Day of the great procession of the Virgen del Carmen and begins at 3:00 p.m. The wonderful and unique thing about this festivity is the unfolding of the dancers dressed in bright colors and masks, they go up to the ceilings and balconies making movements that follow the musical figure of the band. There are also the saqras who are in charge of waiting for the "Patroness of the mestizos". This day is considered the day of the blessing and the parade goes to the cemetery, to show their gratitude to the deceased dancers.</p> <br>
											<h4>July 18</h4>
                                            <p>Date when the virgin is taken to the temple and the children expect to be blessed by the priest, performing the traditional "ocarikuy". At 4:00 pm. the celebration is concluded with the priest's blessing to the entire population.</p> <br>
											<h4>July 19</h4>
                                            <p>It is the last day of joy. From 11:00 a.m. the image is placed on her altar and the change of clothing for the sacred sculpture begins, a very special ceremony for the residents. They say goodbye praying and accompanying until the last moment the Virgen del Carmen.</p> <br>
										<div class="text-center">
                                                <img src="../img/blog/virgen-del-carmen-danza-tipica.jpg" id="img-blog" alt="Danza en Paucartambo">
                                            </div> <br><br>
                                            <p>The Virgen del Carmen was crowned by Pope John Paul II during his pilgrimage to Peru in 1985. This event is celebrated today in the Andean world, as a very important event by the ceremony held in Sacsayhuamán, in the city of Cuzco. It is worth mentioning that the festivity of the Virgen del Carmen de Paucartambo was declared Intangible Cultural Heritage by the National Institute of Culture, on April 11, 2006. </p>
