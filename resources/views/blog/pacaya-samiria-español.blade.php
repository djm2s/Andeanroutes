                                            <h5>DESCRIPCIÓN:</h5>
                                            <p>La Reserva Nacional Pacaya Samiria (RNPS) es un área protegida ubicada en la región de Loreto Perú, Amazonía peruana. Con un área de 2.08 millones de ha (20,800 kilómetros cuadrados) es la Reserva Nacional más grande del Perú. La extensión de la reserva representa el 1.5% del territorio peruano y el 6% de la región de Loreto también es algo más grande que la superficie de Eslovenia y comparable al 50% del territorio de Dinamarca, Suiza, los Países Bajos y Costa Rica, lo que garantiza La representación ecológica y genética de la región. Comprende aproximadamente 208 centros de población con aproximadamente 40 mil personas viviendo en el área y 50 mil en la zona de amortiguamiento. Hay 56 comunidades nativas afiliadas a organizaciones indígenas.</p>
                                            <br>
                                            <h5>OBJETIVOS</h5>
                                            <p> • Promover estudios de flora y fauna en el área. </br>
                                                • Mejorar y ampliar la educación en el área. </br>
                                                • Interesar a la población local sobre los beneficios de la conservación y el buen manejo de la vida silvestre. </br>
                                                • Promover y mejorar el uso de los recursos naturales de acuerdo con el desarrollo ecológico apropiado.</p><br>
                                            <H5>CLIMA</H5>
                                            <p>Cálido y húmedo, con temperaturas que a menudo superan los 34 ° C. Como en todo el Amazonas, tiene dos estaciones distintas: la estación seca, la estación seca o de verano (mayo a octubre) se conoce como el verano amazónico para la formación de playas fluviales de color blanco. la arena y la otra es la temporada de lluvias o de crecimiento (de noviembre a abril), conocida como el invierno amazónico.</p><br>
                                            <H5>HIDROGRAFÍA</H5>
                                            <P>Hay dos ríos principales en la reserva: el río Pacaya, afluente del Ucayali, desemboca en la margen izquierda del canal Puinahua. El río Samiria, afluente del Marañón, fluye por el lado derecho del río. Hay unos 80 lagos. En la confluencia de los ríos Marañón y Ucayali, ante la comunidad Mariscal Castilla, nació nominalmente Elrio Amazonas.</P> <br>
                                            <h5>TURISMO</h5>
                                            <p>Actualmente cuenta con 6 áreas de uso turístico. De estos, Nauta Caño y Yanayacu-Pucate Tibilo-Pastococha son los más visitados por los turistas debido a su proximidad a las ciudades de Iquitos.En Use Tourist Areas se organizan grupos de personas, dedicados al manejo racional de las palmeras amazónicas y a la prestación de servicios turísticos. . Con respecto al segundo, estas personas han sido capacitadas adecuadamente por diferentes organizaciones gubernamentales e internacionales para armar paquetes turísticos dentro del PSNR y brindar un buen cuidado y atención a los visitantes nacionales y extranjeros. Además, se acondicionaron cómodos refugios con capacidad para albergar a un pequeño número de visitantes (no más de 8) para reducir el impacto. No es posible ingresar al PSNR por cuenta propia, siempre es necesario adquirir los servicios de una agencia de viajes con licencia, un refugio, un crucero (embarcaciones de lujo) o contactar directamente con grupos organizados dentro de la reserva. El tiempo mínimo requerido para visitar esta área protegida es de 3 días y se recomienda tomar de 5 a 7 días. También es posible ingresar a la reserva como guardabosques voluntario.</p><br>
                                            <h5>VOLUNTARIADO </h5>
                                            <p> La Reserva Nacional Pacaya Samiria tiene un programa de voluntariado dirigido por el jefe del área protegida (que depende del SERNANP). Uno de los requisitos para los extranjeros es tener una gestión intermedia de habla castellana. Para acceder al programa deberá ponerse en contacto con el jefe de la reserva nacional. El voluntario vivirá con los guardaparques y el apoyo en la realización de su trabajo de capacitación de las comunidades locales, la implementación de programas de gestión de recursos naturales.</p><br>
