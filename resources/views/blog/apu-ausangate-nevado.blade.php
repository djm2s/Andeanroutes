                                            <h5>Apu Ausangate</h5>
                                            <p>Es la quinta montaña más alta del Perú, se le llama el "Creador de las Aguas", ya que su masa glacial da origen a la laguna Sibinacocha, la segunda más grande del Cusco, que a su vez forma una serie de bofedales que preservan la flora y fauna andina. Sin embargo, el recurso hídrico juega un papel fundamental en la electrificación de la región.</p>
                                            <p>El 60% de esas aguas van al río Vilcanota, lo más sorprendente es que estas aguas sirven para mover las máquinas de la Central Hidroeléctrica de Machu Picchu en las épocas secas.</p>
                                            <p>Otro aspecto que debemos de tener en cuenta es que en la Cosmovisión Andina en la región de Cusco, se considera a esta montaña como el Apu más influyente y más importante debido a la energía espiritual que emana, además que para las personas de la zona, el Ausangate es el origen del agua por ende de la vida y de los campos del lugar.</p>
                                            <br>
                                            <h5>Locación:</h5>
                                            <p>Se encuentra en la cordillera de los Andes, ubicada a unos 100 kilómetros al sureste de la ciudad de Cusco en una sección llamada Cordillera de Vilcanota, que corresponde a la región de Cusco en Perú.</p>
                                            <br>
                                            <h5>Altitud:</h5>
                                            <p>La principal cima de esta montaña está a 6,384 metros sobre el nivel del mar, con lagunas grandes con un color verde / azul luminoso.</p>
                                            <br>
                                            <h5>¿Cómo llegar?:</h5>
                                            <h6>Por su cuenta:</h6>
                                            <p>Puede tomar el autobús público (Cusco - Tinke), el tiempo de viaje estimado es de 3 horas hasta llegar a Tinke y luego puede tomar un teléfono móvil a Pacchanta, cuando llega a Pacchanta, donde puede caminar y desde allí puede ver el imponente Nevado Ausangate.</p>
