                                            <h5>Cosmovisión andina</h5>
                                            <p>En la mitología inca de esta montaña y los lagos cercanos (como Sibinacocha) nace la energía masculina que fertiliza a la madre tierra Pachamama; Después de un largo flujo, las aguas se pierden en las tierras desconocidas del Amazonas para regresar, para llenar los lagos y los glaciares cada noche convertidos en el río de estrellas o Willkamayu conocido en el oeste como la Vía Láctea.
                                                La comunidad de Chillca, compuesta por pastores de llamas y alpacas, es conocida como la guardiana de estos hermosos lugares. Cada año en el lado norte de Ausangate, la fiesta de Qoyllur Ritt'i (quechua: «estrella de nieve») se celebra desde tiempos inmemoriales. Fiesta del Corpus Christi, durante la cual miles de personas hacen una peregrinación al Señor de Quyllur Ritt'i en el templo de Sinakara (pintura hecha sobre una roca).
                                                Esta peregrinación se incluyó en la Lista Representativa del Patrimonio Cultural Inmaterial de la Humanidad.</p>
                                            <h6>Recomendaciones:</h6>
                                            <ul>
                                                <li>La recomendación principal es que no vayas solo a hacer turismo de aventura en esta montaña nevada, siempre contrata a un operador de viajes ya que se encargará de los detalles de transporte, comida, guía profesional y sobre todo de tu seguridad en cada uno. momento.</li>
                                                <li>Traiga pastillas de soroche llamadas Soroch Phills, para el mal de altura o de lo contrario beba mucho té de las hojas de coca.</li>
                                                <li>Ropa cómoda y abrigada, pantalones de trekking, chaqueta impermeable, poncho para la lluvia y zapatos cómodos.</li>
                                                <li>Aplique protector solar, al menos cada dos horas.</li>
                                                <li>No deje su basura en el lugar, Cuidar el medio ambiente.</li>
                                            </ul>
