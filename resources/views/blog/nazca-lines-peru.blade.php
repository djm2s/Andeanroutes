                                            <h5>WHERE IS IT LOCATED AND HOW TO GET THERE?</h5>
                                            <p>The Nasca lines and geoglyphs are located in the Ica region, specifically in the valleys of Palpa and Nasca, 450 kilometers south of Lima and near the Pacific Ocean. Located at the edge of the Western Andes, they are 600 meters above sea level. To arrive by land you can travel by tour buses that depart from the cities of Lima, Arequipa and Cusco. There are no scheduled commercial flights to Ica. Without a doubt, to enjoy and observe the Nasca Lines, it is preferable to see them from the air, a space from where the perspective of the figures is unique and unrepeatable. To do this, the visitor can make a trip in a plane.</p>
                                            <br>
                                            <h5>USEFUL DATA</h5>
                                            <p>
                                                • It is necessary to carry original identity documents (DNI or Passport). Foreign citizens who do not carry their original passport may present a copy of their passport and the original of a document issued by their country of origin.
                                                <br> •Accommodation can be found from $ 10 to $ 50 per night in the closest hostels to the town of Nasca.</br>
                                                • Admission is with prior presentation of ticket or ticket. For the overflight it is recommended to make a reservation in advance, mainly during high seasons (June-August).</br>
                                                • The aircraft that fly over this attraction have a qualified guide who will comment on the figures to be observed with the most important information of each of them.
                                            </p>
                                            <br>

                                            <H5 class="text-center">COLIBRI</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/lineas-de-nasca-colibri.jpg" alt="Colibri nazca linesColibi">
                                            </div>
                                            <h4>What is it?</h4>
                                            <p>It is the most remembered of the figures for its harmonic dimensions, the distance between its two wings is approximately 66 meters.</p>
                                            <h4>History and Discovery</h4>
                                            <p>Considered through the years as one of the most representative figures of this tourist attraction, its discovery and subsequent study would suppose religious activities and cults that took place among the settlers of the ancient Nasca.</p>
                                            <h4>Theories and mysteries</h4>
                                            <p>As the historian Maria Rostworowski mentions, the figures were made to be seen from the sky. However, the function of this figure was not to understand the stars, but to pay tribute to a flying God respected and feared by the people. In this way, it would serve as an offering so that this deity does not punish them with absences of rain, which in a dry land like Nasca would produce devastating consequences.</p>
                                            <br>

                                            <H5 class="text-center">MONKEY</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/linea-nazca-mono.jpg" alt="Monkey - mono Nazca">
                                            </div>
                                            <h4>What is it?</h4>
                                            <p>The curiosity of this figure is that it shows the monkey with nine fingers and a tail in the form of a spiral. It is believed that he is representing the Big Dipper. It is also considered one of the most transcendental, with an approximate size of 135 meters.</p>
                                            <h4>History and Discovery</h4>
                                            <p>The discovery of these strokes by the German researcher Maria Reiche was the starting point necessary to feed the theories that had been developing on a possible astronomical origin, where the ancient settlers of Nasca used this gigantic geoglyph as a starting point to study the sky and prepare for the events that could arrive.</p>
                                            <h4>Theories and mysteries</h4>
                                            <p>As mentioned by the researcher, the group of space constellations had a direct relationship with the formation of the tail and hands of the monkey geoglyph. The appearance in the sky of these stars would be directly related to the approach of rainy seasons, extremely important for a land such as Nasca has.</p>
                                            <br>
                                            <H5 class="text-center">BIRD FRIGATE</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/nazca-lines-frigate.jpg" alt="Bird frigate -Nazca lines">
                                            </div>
                                            <h4>What is it?</h4>
                                            <p>With an approximate length of 160 meters, the most characteristic of this figure is in its own head, which is formed by segments of circles that have a radius that varies between 10 and 20 centimeters. In profile, two circles of equal size could represent your eyes.</p>
                                            <h4>History and Discovery</h4>
                                            <p>As mentioned by María Reiche at the time of making the discovery, the immensity of this figure is that if you stand at the end of one wing you can not see the other side. Approximately, it is the equivalent to the measure of 6 football fields.</p>
                                            <h4>Theories and mysteries</h4>
                                            <p>The realization of this geoglyph would be within astronomy, because its alignment would be responding to a configuration of the night sky. It can be seen that it is not aligned with sunsets or the moon. However, if it is aligned with Fomalhaut, a very bright star that is in the Pisces constellation.</p>
                                            <br>
                                            <H5 class="text-center">GIANT BIRD</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/nazca-lines-giant-bird.jpg" alt="Bird frigate -Nazca lines">
                                            </div>
                                            <h4>What is it?</h4>
                                            <p>The immensity of this bird is reflected in its 300 meters long by 54 meters wide. Among the peculiar characteristics of this figure is the neck shaped like a snake whose beak is pointing directly at the sunrise.</p>
                                            <h4>History and Discovery</h4>
                                            <p>The majesty of this earth trace represents the importance it had during the ancient Nasca era. This zoomorphic figure caused such relevance at the time of its discovery that many also began to call it "Ave Fénix" or "Great Bird".</p>
                                            <h4>Theories and mysteries</h4>
                                            <p>He is considered the "Inti Raymi Announcer", the Inca solar festival, because during the morning between June 20 and 23, when an observer is placed on the head of the bird, you can see the sunset exactly at the end of the huge zigzag peak. For this reason, it is considered an ingenious work in terms of precision in order to hit the solstices.</p>
                                            <br>
                                            <H5 class="text-center">SPIDER</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/spider-araña-nazca.jpg" alt="Spider figure - Nazca lines">
                                            </div>
                                            <h4>What is it?</h4>
                                            <p>Being part of a huge network of lines located on the edge of a trapezoid on the earth's surface, it is another of the most outstanding figures thanks to the skill to make your stroke. It measures approximately 46 meters long.</p>
                                            <h4>History and Discovery</h4>
                                            <p>The skill with which this figure was constructed would not only represent an impressive act of art, but since its discovery was related to the constellation of Orion, which is known worldwide for being one of the brightest in outer space.</p>
                                            <h4>Theories and mysteries</h4>
                                            <p>This geoglyph has both astronomical and religious aspects. Both are related to the realization of omens about the rainy season. In this way, when being in a very arid place, the settlers and students used this figure to avoid times of droughts.</p>
