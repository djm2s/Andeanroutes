                                            <h5>Apu Ausangate</h5>
                                            <p>Is the fifth highest mountain in Peru, is called the "Creator of the Waters", because its glacial mass gives rise to the Sibinacocha lagoon, the second largest in Cusco, this in turn forms a series of bofedales that preserve the Andean flora and fauna. However, the water resource plays a fundamental role in the electrification of the region.</p>
                                            <p>60% of these waters go to the Vilcanota river, the most surprising thing is that these waters serve to move the machines of the Machu Picchu Hydroelectric Power Plant in dry seasons. </p>
                                            <p> Another aspect that we must take into account is that in the Andean Cosmovision in the Cusco region, this mountain is considered as the most influential and most important Apu due to the spiritual energy it emanates, in addition to that for the people of the area, the Ausangate is the origin of the water, therefore of the life and the fields of the place.</p>
                                            <br>
                                            <h5>Location:</h5>
                                            <p>It is located in the Andes mountain range, located about 100 kilometers southeast of the city of Cusco in a section called Vilcanota Mountain Range, corresponding to the Cusco region in Peru.</p>
                                            <br>
                                            <h5>Altitude:</h5>
                                            <p>La principal cima de esta montaña está a 6,384 metros sobre el nivel del mar, con lagunas grandes con un color verde/azul luminoso.</p>
                                            <br>
                                            <h5>How to get?:</h5>
                                            <h6>On their own:</h6>
                                            <p>You can take the public bus (Cusco - Tinke), the estimated travel time is 3 hours until you reach Tinke and then you can take a mobile to Pacchanta, when you arrive at Pacchanta where you can hike and from there you can see the imposing Nevado Ausangate .</p>
