                                            <h5>Andean worldview</h5>
                                            <p>In the Inca mythology of this mountain and the nearby lakes (such as Sibinacocha) the masculine energy is born that fertilizes the mother earth Pachamama; after a long flow, the waters are lost in the unknown lands of the Amazon to return, to fill the lakes and glaciers every night turned into the river of stars or Willkamayu known in the west as the Milky Way.
                                                The Chillca Community, made up of llama and alpaca herders, is known as the guardian of these beautiful places. Every year on the north side of Ausangate, the feast of Qoyllur Ritt'i (Quechua: «star snow») is celebrated from time immemorial. Corpus Christi feast, during which thousands of people make a pilgrimage to the Lord of Quyllur Ritt'i in the temple of Sinakara (painting made on a rock).
                                                This pilgrimage was included in the Representative List of the Intangible Cultural Heritage of Humanity.</p>
                                            <h6>Recommendations:</h6>
                                            <ul>
                                                <li>The main recommendation is that you do not go on your own to do adventure tourism in this snow-capped mountain, always hire a travel operator since they will take care of the details of transport, food, professional guide and above all your safety in each moment.</li>
                                                <li>Bring soroche pills (Soroche Pills) for altitude sickness or otherwise drink plenty of tea from the coca leaves.</li>
                                                <li>Comfortable and warm clothing, trekking pants, waterproof jacket, rain poncho and comfortable shoes.</li>
                                                <li>Apply sun-block, at least every two hours.</li>
                                                <li>Don't leave your trash in the place, take care of the environment.</li>
                                            </ul>
