                                            <h5>¿DÓNDE ESTÁ UBICADO Y CÓMO LLEGAR? </h5>
                                            <p> Las líneas y geoglifos de Nasca se encuentran en la región de Ica, específicamente en los valles de Palpa y Nasca, a 450 kilómetros al sur de Lima y cerca del Océano Pacífico. Ubicados en el borde de los Andes occidentales, se encuentran a 600 metros sobre el nivel del mar. Para llegar por tierra, puede viajar en autobuses turísticos que salen de las ciudades de Lima, Arequipa y Cusco. No hay vuelos comerciales programados a Ica. Sin duda, para disfrutar y observar las Líneas de Nasca, es preferible verlas desde el aire, un espacio desde donde la perspectiva de las figuras es única e irrepetible. Para hacer esto, el visitante puede hacer un viaje en avión.</p>
                                            <br>
                                            <h5>DATOS ÚTILES </h5>
                                            <p>• Es necesario llevar documentos de identidad originales (DNI o pasaporte). Los ciudadanos extranjeros que no porten su pasaporte original pueden presentar una copia de su pasaporte y el original de un documento emitido por su país de origen.
                                                <br> • Se puede encontrar alojamiento de $ 10 a $ 50 por noche en los albergues más cercanos a la ciudad de Nasca. </br>
                                                • La entrada es con presentación previa del boleto o boleto. Para el sobrevuelo, se recomienda hacer una reserva con anticipación, principalmente durante las temporadas altas (junio-agosto). </br>
                                                • Las aeronaves que sobrevuelan esta atracción cuentan con un guía calificado que comentará las cifras a observar con la información más importante de cada una de ellas.
                                            </p>
                                            <br>

                                            <H5 class="text-center">COLIBRI</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/lineas-de-nasca-colibri.jpg" alt="Colibri nazca lines Colibi">
                                            </div>
                                            <h4>¿Qué es? </h4>
                                            <p> Es la más recordada de las figuras por sus dimensiones armónicas, la distancia entre sus dos alas es de aproximadamente 66 metros. </p>
                                            <br>
                                            <h4> Historia y descubrimiento </h4>
                                            <p> Considerado a través de los años como una de las figuras más representativas de esta atracción turística, su descubrimiento y posterior estudio supondría actividades religiosas y cultos que tuvieron lugar entre los pobladores de la antigua Nasca. </p>
                                            <br>
                                            <h4> Teorías y misterios </h4>
                                            <p> Como la historiadora Maria Rostworowski menciona, las figuras fueron hechas para ser vistas desde el cielo. Sin embargo, la función de esta figura no era comprender las estrellas, sino rendir homenaje a un Dios volador respetado y temido por la gente. De esta manera, serviría como una ofrenda para que esta deidad no los castigue con ausencias de lluvia, lo que en una tierra seca como Nasca produciría consecuencias devastadoras.</p>
                                            <br>

                                            <H5 class="text-center">MONO</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/linea-nazca-mono.jpg" alt="Monkey - mono Nazca">
                                            </div>
                                            <h4> ¿Qué es? </h4>
                                            <p> La curiosidad de esta figura es que muestra al mono con nueve dedos y una cola en forma de espiral. Se cree que él representa al Big Dipper. También se considera uno de los más trascendentales, con un tamaño aproximado de 135 metros. </p>
                                            <br>
                                            <h4> Historia y descubrimiento </h4>
                                            <p> El descubrimiento de estos trazos por la investigadora alemana Maria Reiche fue el punto de partida necesario para alimentar las teorías que se habían desarrollado sobre un posible origen astronómico, donde los antiguos pobladores de Nasca utilizaron este gigantesco geoglifo como punto de partida para estudiar el cielo y prepárate para los eventos que podrían llegar. </p>
                                            <br>
                                            <h4> Teorías y misterios </h4>
                                            <p> Como mencionó el investigador, el grupo de constelaciones espaciales tuvo una relación directa con la formación de la cola y las manos del geoglifo mono. La aparición en el cielo de estas estrellas estaría directamente relacionada con el acercamiento de las estaciones lluviosas, extremadamente importante para una tierra como la de Nasca. </p>
                                            <br>
                                            <H5 class="text-center">FRIGA DE AVES</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/nazca-lines-frigate.jpg" alt="Bird frigate -Nazca lines">
                                            </div>
                                            <h4>¿Qué es? </h4>
                                            <p> Con una longitud aproximada de 160 metros, lo más característico de esta figura está en su propia cabeza, que está formada por segmentos de círculos que tienen un radio que varía entre 10 y 20 centímetros. En el perfil, dos círculos de igual tamaño podrían representar tus ojos. </p>
                                            <br>
                                            <h4> Historia y descubrimiento </h4>
                                            <p> Como mencionó María Reiche al momento del descubrimiento, la inmensidad de esta figura es que si te paras al final de un ala no puedes ver el otro lado. Aproximadamente, es el equivalente a la medida de 6 campos de fútbol. </p>
                                            <br>
                                            <h4> Teorías y misterios </h4>
                                            <p> La realización de este geoglifo estaría dentro de la astronomía, porque su alineación respondería a una configuración del cielo nocturno. Se puede ver que no está alineado con las puestas de sol o la luna. Sin embargo, si está alineado con Fomalhaut, una estrella muy brillante que se encuentra en la constelación de Piscis.</p>
                                            <br>
                                            <H5 class="text-center">AVE GIGANTE</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/nazca-lines-giant-bird.jpg" alt="Bird frigate -Nazca lines">
                                            </div>
                                            <h4>¿Qué es? </h4>
                                            <p> La inmensidad de esta ave se refleja en sus 300 metros de largo por 54 metros de ancho. Entre las características peculiares de esta figura está el cuello con forma de serpiente cuyo pico apunta directamente al amanecer. </p>
                                            <br>
                                            <h4> Historia y descubrimiento </h4>
                                            <p> La majestad de este rastro de la tierra representa la importancia que tuvo durante la antigua era de Nasca. Esta figura zoomorfa causó tanta relevancia en el momento de su descubrimiento que muchos también comenzaron a llamarla "Ave Fénix" o "Gran Pájaro". </p>
                                            <br>
                                            <h4> Teorías y misterios </h4>
                                            <p> Se le considera el "Anunciador Inti Raymi", el festival solar inca, porque durante la mañana entre el 20 y el 23 de junio, cuando se coloca un observador en la cabeza del pájaro, se puede ver la puesta de sol exactamente al final de El enorme pico en zigzag. Por esta razón, se considera un trabajo ingenioso en términos de precisión para golpear los solsticios.</p>
                                            <br>
                                            <H5 class="text-center">ARAÑA</H5>
                                            <div class="text-center">
                                                <img src="../img/blog/spider-araña-nazca.jpg" alt="Spider figure - Nazca lines">
                                            </div>
                                            <h4>¿Qué es? </h4>
                                            <p> Al ser parte de una enorme red de líneas ubicadas en el borde de un trapezoide en la superficie de la tierra, es otra de las figuras más destacadas gracias a la habilidad de hacer tu trazo. Mide aproximadamente 46 metros de largo. </p>
                                            <br>
                                            <h4> Historia y descubrimiento </h4>
                                            <p> La habilidad con la que se construyó esta figura no solo representaría un acto de arte impresionante, sino que desde su descubrimiento se relacionó con la constelación de Orión, que es conocida mundialmente por ser una de las más brillantes en el espacio exterior. </p><br>
                                            <h4> Teorías y misterios </h4>
                                            <p> Este geoglifo tiene aspectos astronómicos y religiosos. Ambos están relacionados con la realización de presagios sobre la temporada de lluvias. De esta manera, cuando se encontraban en un lugar muy árido, los colonos y los estudiantes utilizaron esta cifra para evitar tiempos de sequía.</p>
