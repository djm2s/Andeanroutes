                                            <h5>Programa de la fiesta</h5>
                                            <h4>15 de Julio</h4>
                                            <p>Se da inicio a la fiesta con un espectáculo de fuegos artificiales y el sonido de campanazos. Luego ingresa una comparsa y la noche se convierte en cómplice para realizar una fogata. Después un grupo de pobladores escenifica el purgatorio mediante saltos acrobáticos, carreras y diferentes travesuras. A las 11:00 p.m. la banda de músicos le interpreta una serenata a la virgen hasta el día siguiente.</p> <br>
                                            <h4>16 de Julio</h4>
                                            <p>Es el día central de esta majestuosa festividad. Empieza a las 5:00 a.m. con una misa de Aurora, luego a las 10:00 a.m. se da la misa de fiesta donde interpretan las canciones de los Qollas y los Qhapac negros (Antiguas culturas de Paucartambo). Para finalizar este sagrado día en las calles se puede escuchar comparsas, danzas y cantos.</p> <br>
                                            <h4>17 de julio</h4>
                                            <p>Día de la gran procesión de la Virgen del Carmen y empieza a las 3:00 p.m. lo maravilloso y único de esta festividad es el desenvolvimiento de los danzantes vestidos con colores brillantes y máscaras, ellos suben a los techos y balcones realizando movimiento que siguen a la figura musical de la banda. También están los saqras quienes son los encargados de esperar a la “Patrona de los mestizos”. Este día es considerado el de la bendición y el pasacalle se dirige rumbo al cementerio, para demostrar su gratitud a los danzantes fallecidos.</p> <br>
                                            <h4>18 de Julio</h4>
                                            <p>Fecha en que la virgen es llevada al templo y los niños esperan ser bendecidos por el sacerdote, realizando el tradicional “ocarikuy”. A las 4:00 p.m. el festejo se da por finalizado con la bendición del sacerdote a toda la población.</p> <br>
                                            <h4>19 de Julio</h4>
                                            <p>Es el último día de júbilo. Desde las 11:00 a.m. la imagen es colocada en su altar y se da inicio al cambio de ropa de la sagrada escultura, una ceremonia muy especial para los pobladores. Ellos se despiden rezando y acompañando hasta el último momento a la Virgen del Carmen.</p> <br>
                                            <div class="text-center">
                                                <img src="../img/blog/virgen-del-carmen-danza-tipica.jpg" id="img-blog" alt="Danza en Paucartambo">
                                            </div> <br><br>
                                            <p>La Virgen del Carmen fue coronada por el papa Juan Pablo II durante su peregrinaje al Perú en 1985. Este acontecimiento es celebrado hoy en día en el mundo andino, como un acontecimiento muy importante por la ceremonia realizada en Sacsayhuamán, en la ciudad del Cuzco. Cabe mencionar que la festividad de la Virgen del Carmen de Paucartambo fue declarada Patrimonio Cultural Inmaterial por el Instituto Nacional de Cultura, el 11 de abril del 2006. </p>
