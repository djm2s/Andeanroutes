                                            <h5>Historia:</h5>
                                            <p> A Mediados de Julio las calles de Paucartambo se llenan de música, colores, fe y una gran devoción religiosa, la Fiesta de la Mamacha del Carmen reúne el folklore peruano y la mayor cantidad de feligreses, entre el 15 al 18 de Julio de cada año miles de personas se conglomeran con un solo sentir para rendir culto a la mamacha del Carmen, celebrándose el 16 de julio su día central. Son varias las versiones sobre el origen de la festividad en honor a la Virgen del Carmen, en este blog les contamos algunas: </p> <br>
                                            <p>Se cuenta que existían grupos de comerciantes venidos del altiplano, llamados “Qollas”, quienes intercambiaban productos como cañiwa, chuño y quesos con otros con azúcar, licor de caña, chancaca y otros procedentes de la costa. La tradición indica que un día, al momento de bajar los productos que venían del altiplano, uno de los Qollas encontró entre las vasijas la cabeza de la imagen de la Virgen del Carmen. Esto provocó sorpresa y asombro entre los pobladores. La cabeza de la imagen fue entregada a una mujer que ofrecía hospedaje a los viajeros y fue ella quien mandó a confeccionar el cuerpo de la Virgen del Carmen, el cual subsiste hasta la actualidad. Desde entonces los Qollas le rinden pleitesía y honores a la patrona.</p> <br>
                                            <p>Otra versión refiere que en la época colonial existían en Paucartambo haciendas y colonos españoles que se dedicaban a la extracción de minerales en los lavaderos de Qosñipata. Al observar que se propagaban enfermedades como el paludismo y otras que atacaban fácilmente a los lugareños, los hacendados se vieron en la necesidad de llevar esclavos afroperuanos desde la costa para realizar dichos trabajos. Estos nuevos moradores, que eran devotos de la Virgen del Carmen, llevaron la imagen a las haciendas y la veneraban a escondidas. Cuentan que el patrón de una de las haciendas los descubrió y tras convertirse en devoto mandó a construir una capilla para rendirle culto. Esta versión se respalda en la danza Qhapaq Negro, una de las más celebradas en la festividad de la Virgen de Paucartambo.</p>
