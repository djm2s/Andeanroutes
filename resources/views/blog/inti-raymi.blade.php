                                            <h5>The Sun Festival</h5>
                                            <p> Inti Raymi is a Quechua word that means ‘Fiesta del Sol’. Every winter solstice (between June 20 and 23) the Inca performed a ritual in honor of the sun, the highest divinity in the Tahuantinsuyo. Pachacutec started this religious-cultural tradition in the XV century. According to the Andean worldview, at that time the sun began its cycle again. <br>
                                                Formerly the Inti Raymi lasted about 15 days, sacrifices were made and dances were presented to worship the "Sun God". The last Inti Raymi that was made with the presence of the Inca Emperor was in the year of 1535; a year before ¨La Conquista Española¨ the year of 1536. <br>
                                                Currently the Inti Raymi is a theatrical performance, thousands of Cusco and people from all over the world come together to this event that is one of the most important cultural and traditional manifestations in this part of the world. This Inca celebration makes each of us feel the Inca blood run through their veins and revalue our past. </p>
                                            <br>
                                            <h5> When is it celebrated? </h5>
                                            <p> On June 24 of each year, Cusco celebrates the Inti Raymi. This date is due to the fact that it coincides with Peasant's Day. It is the most colorful garment that the jubilee festivals of Cusco have to show the world. </p>
                                            <br>
                                            <h5> Where is it celebrated? </h5>
                                            <p> The staging takes place in 3 specific sites in Cusco with great historical significance: </p>
                                            <br>
                                            <h6> 1st Qoricancha </h6>
                                            <p> The Temple of the Sun or Qoricancha, from the first hours of the day, begins the Festival of the Sun. This representation is performed in the so-called golden garden. It begins with the appearance of the Inca. The first rituals offer coca leaves, llamas, alpacas and more.</p>
                                            <div class="text-center">
                                                <img src="../img/blog/intiraymi-cusco-festivity.jpg" alt="Inti Raymi">
                                            </div><br><br>
                                            <h6> 2nd Plaza de Armas </h6>
                                            <p> The historical center of Cusco is the nerve center of the celebrations of the Sun Festival. The Plaza de Armas is cordoned off. The Inca is carried on litters by eight servants and performs the so-called Coca Ceremony. </p>
                                            <div class="text-center">
                                                <img src="../img/nlog/intiraymi-24-de-junio-tours-fiesta-del-sol.jpg" alt="The sun festival">
                                            </div><br><br>
                                            <h6> 3 ° Sacsayhuaman </h6>
                                            <p> - The Chuquipampa esplanade in the Sacsayhuaman fortress is the main stage of the Inti Raymi representation. This place was very important during the incan period. </p>
                                            <div class="text-center">
                                                <img src="../img/blog/Inti-Raymi-vista-aerea.jpg" alt="The sun festival">
                                            </div><br><br>

                                            <h5> Fun Facts </h5>
                                            <h6> 1.- Inti Raymi is one of the largest festivals in Peru </h6>
                                            <p> The numbers are bombastic. It is estimated that the anniversary was created in 1430 by the Inca Pachacútec. In those times, it lasted for two weeks where there were dances and offerings to the sun-god Inti (hence the name Inti Raymi, which in Quechua means "Feast of the Sun"). In 2019, about 200 thousand people went to Cusco for the festival, which had 700 actors on stage. </p>
                                            <h6> 2.- The Sun Festival marked the beginning of a new year in the Inca calendar </h6>
                                            <p> For ancient peoples, the Inti Raymi represented a new beginning by closing one harvest cycle and opening another. Today, the festival is a way to keep the ancient culture alive and revere the greatness of the Cusco traditions. </p>
                                            <div class="text-center">
                                                <img src="../img/blog/INTI-RAYMI-FESTIVITY-exclusive.jpg" alt="Intiraymi - Inti Raymi">
                                            </div><br><br>
                                            <h6> 3.- Three tourist spots in Cusco are part of the route of the celebration </h6>
                                            <p> The show begins at the Qorikancha Complex, also known as the Temple of the Sun. This monument was erected in the 15th century as a temple of worship to the god Inti and its structure was already adorned with gold plates. From there, the colorful procession passes through the Plaza de Armas, heart of the historic center of Cusco and Cultural Heritage of Humanity since 1983. In the place there is also the Cathedral of Cusco and the Church of the Company of Jesus. The apex of the party takes place on the Esplanada de Sacsayhuamán, where the public watches the show from the stands. In this archaeological complex, located 2 kilometers from the Plaza de Armas, it is possible to admire the magnificent architectural legacy left by the Incas, who built walls of almost 300 meters using only large stones perfectly fitted together. <br> <strong> Qorikancha complex: Santo Domingo street, s / n, Cusco historic center Plaza de Armas or Plaza Mayor de Cusco: between the Commerce Portal, the Belén Portal and the Bread Portal Explanada de Sacsayhuamán: near Don Bosco street.</strong> </p>
                                            <h6> 4.- Inti Raymi today is the Cultural Heritage of the country </h6>
                                            <p> The party was not always so prestigious. In 1572, the viceroy Francisco Álvarez de Toledo characterized her as a pagan and prohibited her. Despite this impediment, the event continued to take place clandestinely until 1944, when its route was reformulated and the date entered the city's official calendar again. The 2019 edition marks the 75th anniversary of the recreation of the festival, which became the Cultural Heritage of the Nation in 2001. </p>
