                                            <h5>Un evento cultural y artístico</h5>
                                            <p> El primero de agosto se celebra el día de la tierra o en quechua: "Pachamama Raymi", en las comunidades alto andinas de Ecuador, Perú, Bolivia y Argentina. La región de los andes se caracteriza por su profundo legado cultural y folclórico proveniente de las culturas Incas y pre-Incas que se expresa en sus fiestas, costumbres y tradiciones. <br>
                                                Es una tradición que continúa desde tiempos ancestrales incas. Muchas personas tienen un estrecho vínculo con nuestra Madre Tierra (Pachamama en quechua), por lo tanto cada 1° de agosto realizan una Ceremonia de Pago a la Pachamama que representa a la tierra en el más amplio sentido de la palabra o sea a toda la naturaleza que nos provee el lugar y los recursos que necesitamos para vivir. <br>
                                                Los incas le guardaban el mayor respeto, era una de sus deidades como lo era el Dios Inti o Sol. Antes de alguna festividad que realizaban, solían hacerle una previa ceremonia de agradecimiento, era el tiempo de que la tierra comiera, bebiera y fumara. Era el tiempo de darle mucho de lo que ella nos ha dado.</p>
                                            <br>
                                            <h5>¿Qué se celebra el día de la Pachamama?</h5>
                                            <p>Es importante saber que la mayoría de comunidades alto andinas han tenido y tienen una profunda dependencia y conexión con la agricultura siendo el mes de agosto el mes en el que normalmente se hacen surcos en la tierra, preparándola para recibir las semillas y por ende las nuevas cosechas. En el día de la Pachamama el hombre andino trata de devolver o retornar hacia la tierra todo lo que ha recibido de ella, demostrando su agradecimiento y pidiéndole que las cosechas venideras sean también abundantes y prosperas. <br>
                                                Dentro de los ingredientes que necesitamos para realizar el agradecimiento están: la hoja de coca, feto de algún animal (de preferencia un animal andino), algunas monedas, lliqlla (manta típica con motivos incas), bebidas alcohólicas (mayormente vino o cerveza), cigarrillos y comida. En las festividades por el día de la Pachamama participan los hombres del campo, las amas de casa, los trabajadores, los agricultores, los niños, las autoridades, las juntas vecinales y otras organizaciones.</p>
                                            <div class="text-center">
                                                <img src="../img/blog/pachamama-pago-a-la-tierra.jpg" id="img-blog" alt="Pago a la tierra - Pachamama">
                                            </div>
                                            <br>
                                            <br>

                                            <h5>¿Cómo se celebra el día de la Pachamama?</h5>
                                            <p>Como parte de las celebraciones por el día de la Pachamama los hombres y mujeres de las comunidades alto andinas se reúnen el primero de agosto en una ceremonia que incluye entre otras cosas:
                                            <ul>
                                                <li>Un hoyo o hueco cavado en el suelo, que es donde se pondrán todas las ofrendas y regalos para la Pacha Mama. Este hoyo tiene una profundidad de 50 centímetros y un diámetro de 1 metro aproximadamente, dependiendo del número de regalos y el tamaño de la ceremonia.</li>
                                                <li>Una manta de lana o ‘Lliclla’ que es la prenda con la que se envolverán los regalos, antes de cubrirlos con tierra.</li>
                                                <li>Fetos de animales, generalmente se utiliza fetos de cordero, alpaca o llama. Simbolizan fertilidad.</li>
                                                <li>Cerveza, comida, hoja de coca, cigarros, confeti, dulces, galletas.</li>
                                            </ul>
                                            Durante la procesión algunos carguyoqs dan regalos a los asistentes, objetos como postales, recuerdos, etc; son repartidos sin distinción alguna. La procesión termina aproximadamente a las 12 del medio día después de que todos los santos regresen a la Catedral donde permanecerán hasta el próximo jueves en que los fieles, el carguyoq, alcalde y demás personas retornan a sus santos hasta su lugar de origen, el templo del que fueron sacados.
                                            <strong>La idea es ‘hacer comer, beber y fumar a la Pacha Mama’. Al final de la ceremonia se coloca madera o leña en el hueco, se envuelve todo lo anterior con la lliclla y finalmente todo es consumido por el fuego, para luego ser cubierto con tierra. Durante toda la ceremonia las personas beben cerveza, consumen diversos platos típicos y celebran con danzas y música.</strong>
                                            </p> <br>
                                            <div class="text-center">
