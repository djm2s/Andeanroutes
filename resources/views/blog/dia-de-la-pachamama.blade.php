                                            <h5>Mitología Inca</h5>
                                            <p>Los incas tenían una religión ‘panteista’ es decir que creían que el Dios creador y la naturaleza (montañas, valles, ríos, rayos, truenos, etc.) eran en realidad una sola entidad o concepto. Los incas no conocían la escritura de modo que la mayoría de las tradiciones y mitos se han transmitido de manera oral de generación en generación. <br>
                                                De acuerdo a la cosmovisión de los Incas, el espacio o tiempo estaba dividido en tres planos:
                                            <ul>
                                                <li> <strong>Hanan Pacha:</strong> Esta palabra significa ‘el mundo de arriba’. Es representado por un cóndor. Era el mundo celestial, reservado para aquellas personas que habían sido justas durante su vida y al cual se accedía a través de un puente elaborado con cabellos. En este mundo superior habitan los dioses más importantes como Wiracocha, como más adelante ampliaremos.</li>
                                                <li><strong>Kay Pacha:</strong> Esta palabra significa ‘el mundo presente, el aquí y ahora’. Es representado por uin puma. Es el mundo donde habitamos los humanos, los animales y algunos Dioses como la Pachamama, Mama Sara y el Pariacaca.</li>
                                                <li><strong>Uku Pacha:</strong> Esta palabra significa ‘el mundo de abajo, el mundo de los muertos’. Es representado por una serpiente. En este mundo según la mitología andina habitan los niños no nacidos, los muertos y todo lo que estaba por debajo de la superficie de la tierra o del mar. Ellos creían que las cuevas, las fuentes y otras aberturas de la superficie terrestre eran en realidad los portales o líneas de conexión entre el Uku Pacha y el Kay Pacha. El Dios más importante de este mundo es llamado ‘Supay’ aunque también habita allí la Diosa Mama Cocha y el Dios Pachacamac.</li>
                                            </ul>
                                            </p>

