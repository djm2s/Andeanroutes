                                            <h3>Itinerary:</h3>
                                            <p>We leave by our private car to the lake of Huaypo, located about 1 hour away. The scenery is wonderful, with rolling hills and a big mountain range in the background, as we use an old dirt road very seldom used, all in the middle of Andean crops, mainly grains. We will do a combination of driving and biking. It takes about an hour to pass above the town of Maras. From there we will continue to the magnificent agricultural terraces of Moray where different levels of terraces are carved into deep bowls/sink holes that were used by the Inca to experiment with and adapt Andean crops to the altitude. We will enjoy a picnic lunch there and then return a few kilometers toward Maras where we will then ride to the terraced Salinas salt pans. A hot spring at the top of the valley discharges a small stream of heavily salt-laden water, which is diverted into salt pans and evaporated to produce a salt that is used for cattle licks. We get to see them from the road and then descend to them. Here we leave the bikes and hike in the middle of the salt ponds to the valley of the Urubamba river below.</p>
                                            
