                    <h5>ITINERARIO:</h5>
                    <p><strong>DAY 1. Welcome in the Lima airport , City tour in Lima with visit to the Huaca
                            Pucllana</strong></p>
                    <p>
                        This day we will be waiting for you at the Lima airport at the time of your flight arrival to
                        give you a warm welcome at the international airport <strong> "Jorge Chávez"</strong> and
                        transfer you to the
                        Hotel.<br>
                        In the afternoon we will pick you up from your hotel to start the City tour in <strong> "The
                            City of
                            Kings"</strong> Lima, on the way we will make a stop in the smallest district of Lima,
                        Barranco where
                        highlights its famous Bridge of Sighs, the Hermitage and the monument to Isabel Granada Larco
                        "Chabuca Granda". We will continue our tour in the tourist transportation to Miraflores where we
                        will see the Park of Love located on the boardwalk from where you can take beautiful photos
                        overlooking the Pacific Ocean, we will have a panoramic view of the Huaca Pucllana which is a
                        Pre-Inca religious temple and administrative center inhabited between the III and VIII centuries
                        A.C, then we will take a walk through the park of the olive trees, which date from the
                        Viceroyalty with an area of 23 hectares.<br>

                        With a route of 6 km we will arrive to the historical center of Lima where we will appreciate
                        the Palace of Justice, the Walk of the Naval Heroes, the French Palace and colonial buildings
                        such as the Plaza Mayor, the Government Palace, the Archbishop's Palace, the Basilica Cathedral,
                        the Municipal Palace, Plaza San Martín, among others. We will visit the BCR Museum, where you
                        can appreciate a collection of gold from Peru and a selection of ceramics, goldsmiths and
                        textiles from various Pre-Columbian cultures (subject to museum hours and access to the historic
                        center). Following our tour of the Church and Convent of San Francisco, we will visit its
                        impressive subterranean crypts known as Catacombs.
                        Return to the hotel.
                    </p>
                    <img src="img/tours/lima-peru-tour.jpg" class="img-fluid" alt="Tour en Lima Perú"><br><br>
                    <p><strong>DAY 2. Travel by Touristic Bus from Lima to Paracas and from Paracas to Nazca.</strong>
                    </p>
                    <p>
                        After having breakfast, we will transfer you to the Cruz del Sur bus terminal where we will
                        board the VIP Cruise bus to Paracas, Once in Paracas our staff will be waiting to transfer you
                        to the port of<strong> Paracas,</strong> where we will start the tour to the Ballestas Islands
                        passing through
                        "El Candelabro" a giant image on the side of a hill. When you arrive to the <strong> Ballestas
                            Islands,</strong>
                        you will visit the different islands for two hours, observing the different species of sea
                        lions, seals, penguins and guano birds. After arriving at the port we will make the tour to the
                        sea lions reserve "Paracas". In the afternoon we will go to the Paracas terminal, from where we
                        will take the bus to the city of Nazca.
                    </p>

                    <p><strong>DAY 03: Flight over the Nazca Lines and Travel to Arequipa.</strong></p>
                    <p>
                        After breakfast, we will transfer you to the<strong> Nazca</strong> airfield for a 45-minutes
                        flight over the
                        Pampas of Nazca, where the ancient pre-Incas drew giant figures of animals and plants with a
                        purpose not yet discovered, many believe that these figures were made by extraterrestrials
                        visitors, where we will be able to see the monkey, the spider, the hummingbird, etc. then we
                        will go to visit the ¨Cemetery of Chauchillas¨ to later go to the Nazca bus terminal for your
                        trip to Arequipa, the travel time will be 9 hours. In Arequipa we will be waiting for you with a
                        sign with your name to transfer you to your hotel.
                    </p>
                    
                    <p><strong>DAY 04: Free Morning and City tour Arequipa.</strong></p>
                    <p>
                        This day you will have the morning free to rest or to go for a walk.

                        In the afternoon, we will make the tour to the <strong> "White City"</strong> including the
                        Convent of Santa
                        Catalina, without a doubt the gem of Arequipa, finally opened to the public in the year 1970,
                        after having been for 400 years in seclusion, with an innumerable amount of pieces of colonial
                        art. Also, visit the Plaza de Armas, the cloisters of the Company of Jesus and the neighborhoods
                        of Cayma and Yanahuara, with a magnificent view of the Misti volcano, and visit a traditional
                        colonial mansion called the Founder's Mansion. At night we will rest at the hotel.
                    </p>

                    <p><strong>DAY 05: Tour in the Colca Canyon.</strong></p>
                    <p>
                        Early in the morning, we will travel by bus for about 4 hours in our way to the Colca Canyon,
                        one of the deepest Canyons in the world (3400 m.a.s.l.), Passing by<strong> Pampa
                            Cañahuas,</strong> located in
                        the National Reserve of Aguada Blanca, habitat of vicuñas and alpacas (camelids from South
                        America). The highuest point of our trip will be at 4900 m.a.s.l. In the Route we will
                        appreciate pre columbian terraces that are still being cultivated by the Collaguas. Arriving to
                        the Colca Valley we will transfer you to your Hotel.<br>

                        In the afternoon, we recommend you to rest and acclimate to the height. If you are in good
                        physical conditions, you will have free time to take a walk around the hotel or take a
                        refreshing bath in the nearby hot springs.
                    </p>
                    <img src="img/condor.png" class="img-fluid" alt="Mirador del Condor"><br><br>
                    <p><strong>DAY 06: Tour in the Colca Canyon and Cruz del Cóndor, return to Arequipa and travel to
                            Puno.</strong></p>
                    <p>
                        Very early in the morning we will go to <strong> La Cruz del Cóndor,</strong> where you can
                        usually observe the
                        Condor which is the largest bird in the world in mid-flight. During the return we will visit the
                        villages of Yanque, Coporaque, Achoma and Maca.Good opportunity to appreciate the life of the
                        inhabitants of these towns that has not changed much since the Inca times. In the afternoon
                        after lunch we will take the Touristic in our way to Puno, where we will be waiting for you to
                        transfer you to your hotel in <strong> Puno.</strong>
                    </p>


                    <p><strong>DAY 07: Lake Titicaca Tour, Floating Islands of Uros and Taquile.</strong></p>
                    <p>
                        We will pick you up at your hotel at 7:00 a.m. approximately to go to the port to start the tour
                        in a boat to the amazing and majestic "Uros Islands", arriving there at 07:20 a.m.
                        approximately.<br>
                        The Uros: These islands are made of artificial floors, made with totora superimposed and
                        skillfully interwoven. During the trip the guide provides extensive information about the Lake
                        Titicaca, the legends of the Uros, their uses, customs and folklore Tour <strong> Lago
                            Titicaca</strong>
                        Taquile: Arriving at Taquile, we will go to the Plaza de Armas, and then have free time to make
                        purchases.<br>
                        The <strong> Taquile Island</strong> presents textile art of an impressive quality comparable
                        only to the looms of
                        the Paracas or Egypt culture and were declared Cultural Patrimony of Humanity. At 12:40 at noon
                        you will taste an exquisite lunch, after all we will be leaving from the Port of Taquile at 2:30
                        p.m. and arriving to Puno at 5:30 p.m. to then transfer you to your hotel.
                    </p>
