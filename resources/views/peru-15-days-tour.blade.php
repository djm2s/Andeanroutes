                        In the town of<strong> Aguas Calientes</strong> you will be waiting for our professional tour
                        guide who will
                        guide you to take the bus to the majestic and impressive citadel of<strong> Machupicchu</strong>
                        (altitude 2450
                        masl), the bus trip lasts approximately 25 minutes, together with the guide they will the
                        entrance to Machupicchu where they will have a guided tour of 2 hours with approximately 30
                        minutes being able to visit the Main Square, the Circular Tower, the Sacred Solar Clock, the
                        Royal Rooms, the Temple of the Three Windows and the Cemeteries, finally they will have free
                        time to continue exploring the magical Inca citadel of Machupicchu and return on your own to the
                        town of Aguas Calientes where you can have lunch and take a walk in the town of Aguas Calientes
                        can choose between walking around Aguas calientes, visit the museum of Machupichu or visit the
                        thermal baths.<br>

                        Finally, spend the night at the selected hotel in Aguas Calientes - Machu Picchu.
                    </p>

                    <p><strong>DAY 11: View of the Sunrise and Guided Tour in Machu Picchu with optional ascent to
                            Huaynapicchu Mountain.</strong></p>
                    <p>
                        Excellent opportunity to get up early to observe a beautiful sunrise and also walk around the
                        town of Aguas Calientes, knowing and visiting its hot springs. Then, you will have time to do
                        any of the following activities (on your own):

                        Visit the Machu Picchu Citadel for the second time (Entrance and bus tickets included).
                        For the more adventurous, we recommend to climb Huayna Picchu or Machupicchu Mountain, from
                        where you can appreciate the dimension of Machu Picchu, even more impressive from the top, the
                        distance to go up and down to one of the mountains takes about 3 - 4 hours. This ride is an
                        exciting adventure for travelers who want to see the contrast between wild nature and carved
                        stone that emerges from the earth as a geological miracle.
                        In the afternoon you will take the return bus to the town of Aguas Calientes where you will be
                        able to have lunch and at the indicated time take the returning train from Aguas Calientes town
                        to the City of Cusco, where we will be waiting for you at the train station to transfer you to
                        your hotel.
                    </p>
