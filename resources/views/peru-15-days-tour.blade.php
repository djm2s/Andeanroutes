                    <p><strong>DAY 08: Guided trip by Bus from Puno to Cusco.</strong></p>
                    <p>
                        After breakfast, we will pick you up from the hotel and take you to the Tourist bus station
                        where you will take a tour that lasts (8 hours approximately) where you will enjoy 4 excursions
                        and guided tours, arriving to the Pass of La Raya at 4,335 m.a.s.l. and the Inca Aymara Museum
                        of Pucará, arriving at noon to the city of Sicuani where we will have included a delicious
                        buffet lunch.<br>

                        Then we will continue with our trip; Where each visit lasts (20 to 40 minutes average), we will
                        visit the Inca temple of Raqchi, the temple of Andahuaylillas or Sistine Chapel of America,
                        making your excursion very pleasant. Upon arrival to the imperial city of Cusco we will be
                        waiting for you to transfer you to your hotel in Cusco, where you will spend the night.
                    </p>
                    
                    <p><strong>DAY 09: City Tour in Cusco</strong></p>
                    <p>
                        In the morning you will have free time to do what you prefer, go shopping, walk around the
                        typical streets of Cusco and take pictures around the City.<br>

                        After your lunch, we will pick you up from your hotel at the time indicated by your travel
                        agent, to start and enjoy a guided tour at 1:35 pm where you will be able to know the following
                        tourist attractions of the city of Cusco:

                        <strong>The Temple of the Sun or Qoricancha:</strong> The convent of Santo Domingo in Cusco is
                        the oldest
                        religious building in South America, known as the "Temple of the Sun". Currently you can see the
                        wonderful Inca architecture linked to Spanish architecture which is a sample of our mestizo
                        culture.
                        <strong>The Cathedral:</strong> The Cathedral Basilica of the city of Cusco was built by native
                        Cusco workers and
                        led by the Spaniards. It also has the finest expressions of colonial goldsmithing as well as
                        wood carving and a valuable collection of canvases of the "Escuela Cusqueña" (School of
                        Cusco).<br>
                        <strong>Saqsayhuaman: </strong> is one of the Inca constructions that most astonishes tourists
                        who, according
                        to new hypotheses, point out that it would have been an important Inca ceremonial center and not
                        a fortress as previously believed. Its construction took more than seven decades and required
                        the strength of about 20,000 men.<br>
                        <strong>Qenqo:</strong> Ceremonial and ritual center, it was an amphitheater where sacrifices
                        were made, it is
                        said that it was an Inca shrine. Its construction is made of stones. There are passages, canals
                        and staircases with engravings that represent the cougar, a very sacred animal for the Incas.
                        <strong>Puca Pucara: </strong> tambo guard place along the Inca roads, this archaeological site
                        has:
                        enclosures, interior plazas, baths, aqueducts, watchtowers and an easily recognizable Inca trail
                        in synthesis the urban layout is highly adequate and functional.<br>
                        <strong>Tambomachay: </strong> its stairways you can see crystalline waterfalls and, according
                        to what is
                        affirmed here, the liquid element for the Incas, "the water", was worshiped.
                        Returning to Cusco at 6:45pm.
                    </p>

                    <p><strong>DAY 10: Travel by Train to Machupicchu, Overnight stay in Aguas Calientes.</strong></p>
                    <p>
                        Very early according to the train service you chose:<strong> Expedition or Vistadome
                        </strong>(Vistadome
                        Service adding US$ 45), we will pick you up from your hotel in Cusco to take you to the Poroy
                        train station (25 minutes from Cusco), where you will take the train to Aguas Calientes town
