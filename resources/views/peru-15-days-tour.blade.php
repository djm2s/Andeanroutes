                    <p><strong>DAY 12: Tour in the Sacred Valley of the Incas.</strong></p>
                    <p>
                        We will go to pick you up at your hotel at the time indicated by the Travel agent, starting the
                        tour at 09:00 am. In this tour you will visit the town of Pisac. Located 32 km from the city of
                        Cusco. Pisaq is a typical Andean artisan and archaeological town where the famous Handicraft
                        Fairs are held in their typical markets where you can make purchases and enjoy a guided tour in
                        the archaeological sites of the place; continuing with our tour of approximately one hour we
                        will pass through different towns such as: Coya, Lamay, the province of Calca and Yucay
                        appreciating the beauty of the Sacred Valley of the Incas. We will pass to the town of Urubamba
                        where we will enjoy a delicious buffet lunch in the cozy restaurant “Tunupa”; Later we will go
                        to the town of Ollantaytambo (It is the only Incan city in Peru that is still inhabited) to
                        visit the archaeological complex of the same name; having been an agricultural, administrative,
                        social, religious and military complex in the Tahuantinsuyo. At the end returning to the city of
                        Cusco we will visit the archaeological complex of Chinchero and its chapel, arriving to Cusco at
                        about 07:00 p.m.
                    </p>
