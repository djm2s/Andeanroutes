                        petroglyphs or rock paintings of Toro Muerto, the largest center or repository of rock art in
                        the world, there were approximately 5,000 giant boulders of volcanic magma or recorded with
                        different shapes. The subjects of the paintings include thousands of anthropomorphic characters
                        (dancers, musicians, shepherds, hunters, and realistic figures semirealistas), zoomorphic
                        (jaguars, condors, snakes, eagles, camels, fish), phytomorphic: sunflowers and trees without
                        branches, geometric symbols , zigzags, squares, diamonds and even incomplete figures under
                        development, recorded by various ethnic groups and cultures Callahuaya Wari. In the afternoon he
                        will be taken to one of the restaurants in the region (Punta Colorada), where passengers will be
                        delighted with a typical lunch with delicious shrimp, good Paracas and other varieties of
                        traditional cuisine. Return to the city of Arequipa, crossing the desert and transfer to your
                        hotel.
