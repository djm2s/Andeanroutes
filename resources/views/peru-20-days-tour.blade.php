                    <p><strong>DAY 12: Majes Valley and Toro Muerto Petroglyphs</strong></p>
                    <p>
                        After taking the first meal of the day, he collected for his departure from the city for the
                        province of Castilla (Majes Valley), north-west of Arequipa, 163 km Arrival Majes Valley, then
