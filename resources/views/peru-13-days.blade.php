                    <p><strong>Day 6: Guided trip by Bus from Cusco to Puno.</strong></p>
                    <p>
                        We will pick you up from your hotel at 6:20 am to transfer you to the bus terminal for your trip
                        to Puno (altitude 3818 m.a.s.l.).<br>
                        Our Comfortable bus is equipped with toilets and is run by three people: a driver, a
                        professional bilingual guide and an attendant. We will make a small visit to the church of
                        <strong>Andahuaylillas,</strong> the church that dates from the early seventeenth century and It
                        is known as the
                        Sistine of South America for the magnificent paintings it has and for its altar chiseled in
                        gold. After our visit we will keep going 218 km to Raqchi, where the Inca temple of Wiracocha is
                        located.<br>
                        After visiting Raqchi We will continue our way to Sicuani where we will enjoy a delicious buffet
                        lunch to go after to La Raya, located at 4,335 meters high. Our next destination will be Pucara,
                        where we will visit the Lithic Museum and finally our tour will be through high Andean
                        esplanades under an unreal clarity.<br>
                        In the city of Puno our staff will be waiting for you to take you to your Hotel.
                    </p>

                    <p><strong>Day 7: Lake Titicaca tour, Los Uros and Taquile.</strong></p>
                    <p>
                        We will pick you up at your hotel at 7:00 a.m. approximately to go to the port to start the tour
                        in a boat to the amazing and majestic<strong> "Uros Islands",</strong> arriving there at 07:20
                        a.m.
                        approximately.<br>
                        The Uros: These islands are made of artificial floors, made with totora superimposed and
                        skillfully interwoven. During the trip the guide provides extensive information about the Lake
                        Titicaca, the legends of the Uros, their uses, customs and folklore.<br>
                        Taquile: Arriving at Taquile, we will go to the Plaza de Armas, and then have free time to make
                        purchases.<br>
                        The<strong> Taquile Island</strong> presents textile art of an impressive quality comparable
                        only to the looms of
                        the Paracas or Egypt culture and were declared Cultural Patrimony of Humanity. At 12:40 at noon
                        you will taste an exquisite lunch, after all we will be leaving from the Port of Taquile at 2:30
                        p.m. and arriving to Puno at 5:30 p.m. to then transfer you to your hotel.
                    </p>

                    <p><strong>Day 8: Travel by Bus from Puno to Colca Canyon.</strong></p>
                    <p>
                        Early in the morning, you will travel by bus to have an incredible 6-hours trip to the Colca
                        Canyon, one of the deepest canyons in the world (3,400 mts.), Passing through Pampa Cañahuas,
                        located in the Aguada Blanca National Reserve , habitat of vicuñas and alpacas (camelids of
                        South America). The highest point of the route is 4,900 masl. on the route we will appreciate
                        pre-Columbian terraces that are still cultivated by the native collaguas. Arriving to the Colca
                        Valley, we will transfer you to your respective hotel.<br>

                        In the afternoon, we recommend you to rest and acclimate to the height. If you are in good
                        physical condition, you can enjoy your free time to take a walk around the hotel or take a
                        refreshing bath in the nearby hot springs.
                    </p>

                    <p><strong>Day 9: Colca Canyon tour and Travel by bus to Arequipa.</strong></p>
                    <p>
                        Very early in the morning we will go to La Cruz del Cóndor, where you can usually observe the
                        largest bird in the world in mid-flight called The Condor. During the return we will visit the
                        villages of Yanque, Coporaque, Achoma and Maca. Good opportunity to appreciate the life of the
                        inhabitants of these towns that has not changed much since the Inca times. Finally, in the
                        afternoon, we will return to the White City of Arequipa.
                    <p>
                        <img src="img/responsive/valle-sagrado-sacred-valley.jpg" class="img-fluid max-width: 100%"
                            alt="Sacred valley od the incas">
                    <p><strong>Day 10: Free Morning and City tour Arequipa.</strong></p>
                    <p>
                        This day you will have the morning free to rest or to go for a walk.<br>

                        In the afternoon, we will make the tour to the "White City" including the Convent of Santa
                        Catalina, without a doubt the gem of Arequipa, finally opened to the public in the year 1970,
                        after having been for 400 years in seclusion, with an innumerable amount of pieces of colonial
                        art. Also visit the Plaza de Armas (Main Square), the cloisters of the Company of Jesus and the
                        neighborhoods of Cayma and Yanahuara, with a magnificent view of the Misti volcano, and visit a
                        traditional colonial mansion called the Founder's Mansion. At Night, we will transfer you from
                        your hotel to the bus station to travel by bus from Arequipa to Nasca.
                    <p>
                        <img src="img/responsive/llama-from-cusco.jpg" class="img-fluid max-width: 100%"
                            alt="Llama de cusco animal">
                    <p><strong>Day 11: Nasca Lines Overflight.</strong></p>
                    <p>
                        We will pick you up from the bus station at 7:00 am to transfer you to the Nasca airfield and
                        start the overflight on the Nasca lines for an approximate time of 45 minutes.<br>

                        The Nasca Lines are mysterious strokes made, on the sand covering an area of ​​350 km, they
                        represent figures of different animals and plants such as the Spider, The Monkey, The Dog, the
                        Lizard, the Hummingbird, among others, in large dimensions that reach 15 300 meters each and
                        with a depth of 30 centimeters approx.<br>

                        According to Dr. María Reiche, who has devoted a great part of her life to the study of these
                        lines, it is the largest astronomical calendar in the world.<br>

                        In the afternoon we will take the bus to Paracas, 3 hours from Nazca, where our staff will be
                        waiting for you.
                    <p>
                    <p><strong>Day 12: Ballestas Islands Tour, Reserve of sea Lions of Paracas and Huacachina Oasis.
                        </strong></p>
                    <p>
                        After you will enjoy breakfast at your hotel, we will pick you up from your hotel to begin the
                        Tour in the Islands Ballestas, we will pass for the Chandelier “Candelabro” a giant image on the
                        side of a hill.<br>

                        Arriving at the ballestas islands, you will visit the different islands for two hours observing
                        the differents species of sea lions, seals penguins and guano birds, ending the tour we will go
                        to the Paracas station for our trip to Ica where you will have lunch by yourself and where you
                        will make the City tour in Ica, visit the vineyard farm and later we will cross the desert of
                        the Huacachina Oasis in tubular buggies climbing immense sand dunes, in this ride the adrenaline
                        will be our accomplice to free ourselves from stress and live a truly unforgettable
                        experience. During this trip you will also have the opportunity to practice sandboarding.<br>

                        If the weather conditions are favorable we will have the opportunity to see the sunset in the
                        desert of Ica. Finally at 19:30 we will take a bus to Lima where we will spend the night in a
                        hotel.
