                    <h5>ITINERARY:</h5>
                    <p><strong>Day 1: Arrival in Lima</strong></p>
                    <p>
                        This day we will be waiting for you at the<strong> Lima</strong> airport at the time of your
                        flight arrival to
                        give you a warm welcome at the international airport "Jorge Chávez" and transfer you to the
                        Hotel.<br>
                        In the afternoon we will pick you up from your hotel to start the City tour in<strong> "The City
                            of
                            Kings"</strong> Lima, on the way we will make a stop in the smallest district of Lima,
                        Barranco where
                        highlights its famous Bridge of Sighs, the Hermitage and the monument to Isabel Granada Larco
                        "Chabuca Granda". We will continue our tour in the tourist transportation to Miraflores where we
                        will see the Park of Love located on the boardwalk from where you can take beautiful photos
                        overlooking the Pacific Ocean, we will have a panoramic view of the Huaca Pucllana which is a
                        Pre-Inca religious temple and administrative center inhabited between the III and VIII centuries
                        A.C, then we will take a walk through the park of the olive trees, which date from the
                        Viceroyalty with an area of 23 hectares.<br>

                        With a route of 6 km we will arrive to the historical center of Lima where we will appreciate
                        the Palace of Justice, the Walk of the Naval Heroes, the French Palace and colonial buildings
                        such as the Plaza Mayor, the Government Palace, the Archbishop's Palace, the Basilica Cathedral,
                        the Municipal Palace, Plaza San Martín, among others. We will visit the BCR Museum, where you
                        can appreciate a collection of gold from Peru and a selection of ceramics, goldsmiths and
                        textiles from various Pre-Columbian cultures (subject to museum hours and access to the historic
                        center). Following our tour of the Church and Convent of San Francisco, we will visit its
                        impressive subterranean crypts known as Catacombs.<br>
                        Return to the hotel.
                    </p>

                    <p><strong>Day 2: Welcome at the Airport and City tour in Cusco.</strong></p>
                    <p>
                        This day begins with the pick up in your hotel for your flight from Lima to Cusco that you will
                        take at the "Jorge Chávez" airport in the city of Lima with 2 hours in advance of your flight to
                        the "Archaeological Capital of America" Cusco (Altitude 3350 m.a.s.l.), where our staff will be
                        waiting for you at the airport with a sign with your name to transfer you to the selected hotel,
                        where you will be offered a coca leaves tea which is good for the acclimatization of Cusco.
                        After your lunch, we will pick you up from your hotel at the time indicated by your travel
                        agent, to start and enjoy a guided tour at 1:35 pm where you will be able to know the following
                        tourist attractions of the city of Cusco:<br>
                        <strong> The Temple of the Sun or Qoricancha:</strong> The convent of Santo Domingo in Cusco is
                        the oldest
                        religious building in South America, known as the "Temple of the Sun". Currently you can see the
                        wonderful Inca architecture linked to Spanish architecture which is a sample of our mestizo
                        culture.<br>
                        <strong> The Cathedral:</strong> The Cathedral Basilica of the city of Cusco was built by native
                        Cusco workers and
                        led by the Spaniards. It also has the finest expressions of colonial goldsmithing as well as
                        wood carving and a valuable collection of canvases of the "Escuela Cusqueña" (School of Cusco).
                        Saqsayhuaman: It is one of the Inca constructions that most astonishes tourists who, according
                        to new hypotheses, point out that it would have been an important Inca ceremonial center and not
                        a fortress as previously believed. Its construction took more than seven decades and required
                        the strength of about 20,000 men.<br>
                        <strong>Qenqo:</strong> Ceremonial and ritual center, it was an amphitheater where sacrifices
                        were made, it is
                        said that it was an Inca shrine. Its construction is made of stones. There are passages, canals
                        and staircases with engravings that represent the cougar, a very sacred animal for the
                        Incas.<br>
                        <strong>Puca Pucara:</strong> Former tambo guard place along the Inca roads, this archaeological
                        site has:
                        enclosures, interior plazas, baths, aqueducts, watchtowers and an easily recognizable Inca trail
                        in synthesis the urban layout is highly adequate and functional.<br>
                        <strong>Tambomachay:</strong> Through its stairways you can see crystalline waterfalls and,
                        according to what is
                        affirmed here, the liquid element for the Incas, "the water", was worshiped.
                        Returning to Cusco at 6:45pm.
                    </p>

                    <p><strong>Day 3: Tour of the Sacred Valley of the Incas and Travel by Train to Machupicchu (Aguas
                            Calientes).</strong></p>
                    <p>
                        Pisaq is a typical Andean artisan and archaeological town where the famous Handicraft Fairs are
                        held in their typical markets where you can make purchases and enjoy a guided tour in the
                        archaeological sites of the place; continuing with our tour of approximately one hour we will
                        pass through different towns such as: Coya, Lamay, the province of Calca and Yucay appreciating
                        the beauty of the<strong> Sacred Valley of the Incas.</strong> We will pass to the town of
                        Urubamba where we will
                        enjoy a delicious buffet lunch in the cozy restaurant “Tunupa”; Later we will go to the town of
                        Ollantaytambo (It is the only Incan city in Peru that is still inhabited) to visit the
                        archaeological complex of the same name ; having been an agricultural, administrative, social,
                        religious and military complex in the Tahuantinsuyo, ending the tour at approximately 4:00 pm,
                        then you will have free time in the town of<strong> Ollantaytambo,</strong> where you can visit
                        their craft
                        market, their small streets that still keeps the layout of the Inca streets, seeing the water
                        run through its old canals, is like going back in time, ideal for taking pictures or having a
                        coffee in its Main Square.

                        At the indicated time you must take the Ollantaytambo train leaving at 19:00 p.m. to the town of
                        Aguas Calientes where our staff will be waiting for you to transfer you to the selected hotel.
                    </p>
                    <img src="img/tours/lima-peru-tour.jpg" class="img-fluid max-width: 100%"
                        alt="Lima Perú, Tour en Lima, tour 12 días"><br><br>

                    <p><strong>Day 4: View of the Sunrise and Guided Tour in Machu Picchu with optional ascent to
                            Huaynapicchu Mountain.</strong></p>
                    <p>
                        After your breakfast at the hotel, excellent opportunity to get up early and observe a beautiful
                        sunrise in<strong> Machu Picchu,</strong> In coordination with your guide, you will take the bus
                        from Aguas
                        Calientes Town to Machu Picchu (altitude 2450 meters on sea level) (approximately 25 minutes).
                        May observe the sunrise in Machu Picchu takin your guided tour at 7:00 am, 3 hours
                        approximately, being able to visit: the Main Square, the circular tower, the Sacred Solar Clock,
                        the Royal Room, the Temple of the three Windows, and the Cemeteries.<br>

                        Before or after the guided Tour in Machu Picchu, depending on your tickets schedule, You will
                        have time to explore on your own or use your tickets to climb Huayna Picchu or Machu Picchu
                        Mountain (Notice that if you want to climb Huaynapicchu mountain, it must be booked at least 3
                        months in advance because of availability, the entrance times may vary depending on the time of
                        booking). Besides having the guided tour in Machu Picchu citadel, You can also visit one of the
                        following mountains (without guide):<strong> Huaynapicchu Mountain </strong>(from where a
                        spectacular view of the
                        entire valley of Machu Picchu is appreciated) or Machu Picchu Mountain (the mountain that gave
                        its name to Machu Picchu with a unique landscape beauty) the entrance ticket to one of the
                        Mountains will be included and you can do it optionally, any of them will take you 3 hours
                        approximately - round trip. This travel program is ideal for touring Machu Picchu in its
                        entirety and taking pictures with enough time until the time you take it convenient to return by
                        bus to Aguas Calientes so you can have lunch in the same town. In the afternoon you will have to
                        board the train to Poroy or Ollantaytambo.Once you arrive to one of these Train stations our
                        staff will be waiting for you to transfer you to your Hotel in Cusco.<br>

                        The advantage of this tour is that you can get to know Machu Picchu without many people and with
                        much more time available.
                    </p>

