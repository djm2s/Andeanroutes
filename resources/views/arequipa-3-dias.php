                                                <h5>ITINERARIO:</h5>
                                                <p><strong>Día 01: Arequipa, Santa Catalina y Juanita.</strong></p>
                                                <p>A su llegada a Arequipa, su guía local lo saluda para llevarlo al hotel. Después de haber tenido la oportunidad de registrarse en su hotel y refrescarse, se reunirá con su guía para conocer el maravilloso Convento de Santa Catalina y el Museo Santuarios de Altura para ver a la famosa Doncella de Hielo, la momia Juanita. Fuertemente creído haber sido sacrificado a los dioses hace unos 500 años. Juanita fue descubierta en la cima del Monte Ampato, le ha brindado a los científicos una mirada única en el tiempo, ya que está increíblemente bien conservada y vestida con algunas de las mejores telas de Cusco. <strong> Durante la noche en Arequipa. </strong></p><br>
											<p><strong>Day 02: Cañon del Colca</strong></p>
											<p>Hoy partirá de Arequipa a través de la reserva de Aquada Blanca, donde verá vicuñas y varias especies de aves acuáticas de las tierras altas. Los majestuosos volcanes, como Misti, dominan el paisaje, ya que continuará escalando hasta el punto más alto de todo el viaje en el Mirador de los volcanes. Desde este punto de vista, experimentará el impresionante rango volcánico, incluido el famoso Coropuna. <strong>Pernocte en Colca.</strong></p><br>
											
                                            <p><strong>Día 03: Los Cóndores y Aguas Termales</strong></p>
                                            <p>Es temprano en la mañana mientras conduce a la Cruz de los Cóndores, un balcón de roca sobre la sección más profunda del cañón. Es aquí donde presenciarás el majestuoso vuelo de los gigantes cóndores andinos. Su caminata de regreso cerca del cráter de un volcán también pasa por un bosque de cactus. También en el camino a Chivay, visitará paradas coloridas y talleres de bordado. Después del almuerzo hay un paseo opcional en bicicleta seguido de un chapuzón en las aguas termales de la región. Si no va en bicicleta, se dirigirá directamente a las aguas termales. <strong> Pernocte en Colca.</strong></p><br>
											
                                            <p><strong>Día 04: Regreso a casa - Arequipa a Lima</strong></p>
                                            <p>Tendrá la opción de echar un vistazo más a los magníficos Cóndores antes de su traslado de regreso a Arequipa para tomar un vuelo de la tarde a Lima antes de su vuelo de conexión con destino a casa.</p>
