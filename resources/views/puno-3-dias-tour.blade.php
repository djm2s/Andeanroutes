                                            <h5>ITINERARIO:</h5>
                                            <p><strong>Día 01: Fligth a Juliaca - Sillustani –Puno </strong> </p>
                                            <p> Al llegar a Juliaca, estaremos armando para que lo lleve a Puno. En el camino visitaremos Sillustani, un cementerio sagrado de Colla, ubicado al norte de Puno, junto a la hermosa laguna de Umayo y cerca del pueblo de Hatun Colla, apreciaremos las chullpas o recintos funerarios, construidos con piedras pulidas para mantener las momias de su ancestro. <strong> Pernocte en Puno. </strong> </p> <br>
