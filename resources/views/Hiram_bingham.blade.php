                                            <p>This 1920s Pullman-inspired train will take you through the Andes giving you unparalleled views of the passing scenery. Once arriving at Machu Picchu, skip the long lines and head straight to the citadel to take a guided tour of the ruins before heading back to Cusco.</p>
                                            <ul>
                                                <li>Admire the unparalleled views of the passing scenery from the train.</li>
                                                <li>Skip the long lines at Machu Picchu and take a private guided tour.</li>
                                                <li>Try delicious, gourmet meal, wine & pisco during your journey</li>
                                                <li>Enjoy traditional music and dances during the Hiram Bingham experience.</li>
                                            </ul>
                                            <h3>Itinerary:</h3>
                                            <p>Your Machupicchu Experience begins as we pick you up at you hotel, to drive you to Poroy train station, 30 min away from Cusco City. Here you board the Hiram Binghan train journeying through the Sacred Valley of the Incas to the Lost City of Machu Picchu, a trip on the Hiram Bingham takes you across of the most beautiful and stunning scenery in Peru. On the way from Cusco to the miythical lost city of Machu Picchu, the Hiram Bingham goes through Ollantaytambo in the Sacred Valley of the Incas. As you trundle through the spectacular Peruvian countryside, in the comfort of a beautifully restored Pullman carriage, we will serve you a delicious meal from our Andean inspired menu. In the morning, on the way towards Machu Picchu you’ll enjoy a delectable three-course brunch, complete with a refreshing sparkling wine and freshly squeezed orange juice. Upon your arrival at Aguas Calientes, board the a bus specially reserved for Hiram Bingham passengers for the quick ride up to Machu Picchu, avoiding long lines for regular bus service. After enjoying the views, will have a guided tour in Machu Picchu. Afterwards, you have free time to explore the citadel, hike up a little, go to the Temple of the Moon and Inca Bridge, or simply relax and soak in the atmosphere. In the afternoon we will return to Aguas Calientes to catch the train to return to Cusco. In the evening, on the journey from Machu Picchu to Poroy you will be served a luxurious four-course menu, complete with Pisco Sour and Peruvian delicacies. Once in Poroy we will drive you back to the comforts of your hotel in Cusco City. <br> Cusco Arrival at about 9:40pm</p>
