                    <h5>ITINERARY:</h5>
                    <p><strong>Day 1. Welcome and City Tour in the city of the Kings.</strong></p>
                    <p>On the day of your arrival at Jorge Chávez International Airport, our staff will be waiting for you, with a sign bearing your name, and then transport you to the selected hotel. They will be given free time so that they can taste our gastronomy. In the afternoon we will start our tour making a stop in the smallest district of Lima, Barranco where its famous Bridge of Sighs, the Hermitage and the monument to Isabel Granada Larco “Chabuca Granda” stand out. We arrive in Miraflores to visit the Parque del Amor with a panoramic view of the Huaca Pucllana, a pre-Inca temple religious and administrative center inhabited between the 3rd and 8th centuries AD. We will take a walk through the park of olive trees, dating from the Viceroyalty with an area of 23 hectares. With 6 km of travel we will arrive at the gates of the historic center where you will appreciate the Palace of Justice, the Paseo de los héroes Navales, the French Palace and colonial buildings such as the Plaza Mayor, the Government Palace, the Archbishop's Palace, the Cathedral Basilica, the Municipal Palace, the Plaza San Martín, among others. We visit the Museum of the Nation, where you can see a collection of gold from Peru and a selection of ceramics, goldsmiths and textiles from various Pre-Columbian cultures. Continuing our tour of the Church and Convent of San Francisco, the visit to its underground crypts known as Catacombs stands out and then we will return to the hotel.</p>

                    <p><strong>Day 2. Travel by plane from Lima to Cusco and City tour in Cusco.</strong></p>
                    <p>After breakfast, our staff will appear at the hotel to transfer you to the airport and board you by plane from Lima to Cusco and upon arrival in the city of Cusco our staff will be waiting for you, with a sign that will bear your name, and then transport you to the hotel of your choice. You will have free time to rest, adjust to the altitude and eat your meals. Between 1:00 p.m. and 1:30 p.m. we will be passing by your hotel to start the tour, we will visit: The Cathedral, a colonial religious monument of extraordinary artistic value, the Korikancha or Temple of the Sun, where you can appreciate the fine work that the Incas carried out on the stone. Continuing with the tour we will go to Sacsayhuamán, a fortress with a strategic location, which offers an unparalleled view of the city, in this place the Inti Raymi or "Fiesta del Sol" is held every June 24, later we will visit Qenqo, Tambomachay and Puca Pucará, archaeological complexes of a religious nature.</p>

                    <p><strong>Day 3. Tour in the Sacred Valley of the Incas and Aguas Calientes.</strong></p>
                    <p>Sacred Valley here I come! We start the tour passing by your hotel at the agreed time and together with the group we set off to visit the impressive archaeological center of Pisaq and its beautiful town with very friendly local people, here you can buy gifts and typical souvenirs of the area at a very good price. At noon we arrive in Urubamba, where we will taste the most exquisite Andean gastronomy in a local restaurant, we continue our tour to Ollantaytambo, the last Inca town that maintains its original architectural structure, to end this magical tour we will go to the train station, travel towards Aguas Calientes will be a pleasant experience with the most attentive staff. Upon arrival in Aguas Calientes, the hotel staff will wait for you to go to the hotel, where our guide will visit you and give you the explanation and tips for the next day's tour.</p>
                    <p><strong>Day 4. Tour to Machu Picchu "The lost city of the Incas" and hike to Huaynapicchu.</strong></p>
                    <p>¡El gran día llegó! Desayunamos y nos dirigimos de madrugada a la estación de buses, abordaremos el bus rumbo al ingreso principal del Santuario de Machu Picchu. Ingresando junto al grupo, tendremos un guiado en la ciudad perdida de los incas, por un tiempo aproximado de 2:30 hrs realizando paradas en los puntos más importantes y energéticos para relajarnos y tomarnos las mejores fotos de recuerdo. Culminado el tour guiado nuestro guía se despide y contaremos con tiempo libre para poder disfrutar del impresionante paisaje, según su horario de tren bajaremos a Aguas Calientes y abordaremos el tren rumbo a Ollantaytambo, lugar donde nuestro transporte lo espera para trasladarlo a su hotel en la ciudad de Cusco. Importante: No hay un tiempo límite de permanencia en MachuPicchu, pero sí usted sale de la ciudadela no podrá ingresar nuevamente, a menos que cuente con un ingreso adicional o un ingreso a Huaynapicchu o MacuPicchu Montaña, la misma que tenemos incluido para usted en este tour y podrá hacer este recorrido por su cuenta..</p>
                    <p><strong>Day 5. Homecoming</strong></p>
                    <p>Two hours before your flight departure, we will pick you up from your hotel and then transport you to the airport, where we will say goodbye and end our services.</p>
