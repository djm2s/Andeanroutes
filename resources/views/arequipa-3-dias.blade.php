                                            <h5>ITINERARY:</h5>
                                            <p><strong>Day 01: Arequipa, Santa Catalina and Juanita</strong></p>
                                            <p>Upon your arrival to Arequipa, your local guide greets you to drive you to hotel. After you have had a chance to check-in to your hotel and freshen up you will meet up with your guide for an insider’s look at the wonderful Convent of Santa Catalina and the Museum Santuarios de Altura to see the famous Ice Maiden—the mummy Juanita—strongly believed to have been sacrificed to the gods some 500 years ago. Juanita was discovered on the summit of Mount Ampato, she’s provided scientists a unique look back in time as she is amazingly well preserved and clothed in some of the finest fabric from Cusco.<strong> Overnight in Arequipa. </strong></p><br>
                                            <p><strong>Day 02: Colca Canyon</strong></p>
                                            <p>Today you will depart Arequipa via the reserve of Aquada Blanca, where you will see Vicunas and several species of highland water birds. The majestic volcanoes, such as Misti dominate the landscape as you will continue climbing until the highest point in the entire trip at el Mirador de los volcanoes. From this vantage point you will experience the impressive volcanic range—including the famous Coropuna. Overnight in Colca..</p><br>

                                            <p><strong>Day 03: The Condors & hot Springs</strong></p>
                                            <p>It is an early morning as you drive to the Cross of the Condors, a rock balcony over the deepest section of the canyon. It is here you will witness the majestic flight of the giant Andean condors. Your return hike near the crater of a volcano also passes by a cactus forest. Also en route to Chivay, you will visit colourful stop and ateliers of embroidery. After lunch there is an optional bike ride followed by a dip in the region’s hot springs. If not biking, then you will head over directly to the hot springs.<strong> Overnight in Colca..</strong></p><br>

                                            <p><strong>Day 04: Homeward Bound - Arequipa to Lima</strong></p>
                                            <p>You will have the option for one more look at the magnificent Condors before your transfer back to Arequipa to catch an afternoon flight to Lima before your connecting flight homeward bound.</p>
